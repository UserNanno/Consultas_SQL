VENTAS_SOLICITUDES AS (
  SELECT
    A.*,
    TRIM(A.CODINTERNOCOMPUTACIONAL) AS CIC_SOL,  -- ✅ clave única para joins
    B.*,
    C.*
  FROM SOLICITUDESUNICAS A
  LEFT JOIN TP_CUENTACREDITOPERSONAL B
    ON A.NUMSOLICITUDCORTO = B.CODSOLICITUDCORTO
   AND A.DESCANALVENTARBMPER = B.CANAL_MDPREST
   AND TRIM(A.CODINTERNOCOMPUTACIONAL) = TRIM(B.CODINTERNOCOMPUTACIONAL)
  LEFT JOIN M_SOLICITUDCREDITOCONSUMO C
    ON A.CODMESEVALUACION = C.CODMESSOLICITUD
   AND A.NUMSOLICITUDEVALUACION = C.CODSOLICITUD
),



LEFT JOIN LEADS_UNICOS B
  ON A.CIC_SOL = TRIM(B.CODINTERNOCOMPUTACIONAL)
 AND A.LEAD_CEF = B.LEAD_CEF
LEFT JOIN LEADS_UNICOS C
  ON A.CIC_SOL = TRIM(C.CODINTERNOCOMPUTACIONAL)
