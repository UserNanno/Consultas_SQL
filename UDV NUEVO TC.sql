SOLICITUDES_LEAD AS (
  SELECT
    A.CODMES,
    A.FEC_EVALUACION,
    A.CIC,
    A.NUMSOLICITUD_RBM,
    A.NUMSOLICITUD_CORTO,
    A.CANAL_RBM,
    A.RESULTADO_CDA,
    A.DECISION_RBM,
    A.CAMPANIA_RBM,
    A.TIPO_CAMPANIA_RBM,
    A.LEAD_CEF,
    A.MTO_DESEMBOLSO_SOL_VTA,
    A.FEC_SOLICITUD,
    A.MTO_SOLICITADO,
    A.MTO_APROBADO,
    COALESCE(B.MTO_OFERTA, C.MTO_OFERTA) AS MTO_OFERTA_LEAD
  FROM RBM_VTA_SOL A
  LEFT JOIN LEADS_UNICOS B
    ON A.CIC = B.CIC
   AND A.LEAD_CEF = B.LEAD_CEF
  LEFT JOIN LEADS_UNICOS C
    ON A.CIC = C.CIC
),
