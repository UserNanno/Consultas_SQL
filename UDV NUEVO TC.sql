SELECT
  COUNT(*) AS total,
  SUM(CASE WHEN B.CODINTERNOCOMPUTACIONAL IS NOT NULL THEN 1 ELSE 0 END) AS match_exacto,
  SUM(CASE WHEN C.CODINTERNOCOMPUTACIONAL IS NOT NULL THEN 1 ELSE 0 END) AS match_fallback
FROM VENTAS_SOLICITUDES A
LEFT JOIN LEADS_UNICOS B
  ON TRIM(A.CODINTERNOCOMPUTACIONAL) = TRIM(B.CODINTERNOCOMPUTACIONAL)
 AND A.LEAD_CEF = B.LEAD_CEF
LEFT JOIN LEADS_UNICOS C
  ON TRIM(A.CODINTERNOCOMPUTACIONAL) = TRIM(C.CODINTERNOCOMPUTACIONAL);
