SELECT 
  COUNT(*) AS total_filas,
  COUNT(DISTINCT CODINTERNOCOMPUTACIONAL) AS clientes,
  COUNT(DISTINCT CODEVALUACIONSOLICITUD) AS evaluaciones,
  COUNT(DISTINCT CONCAT(TRIM(CODINTERNOCOMPUTACIONAL),'|',TRIM(CODEVALUACIONSOLICITUD))) AS pares_trim
FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_RBMRBMPER_MODELOGESTION_VU.MD_EVALUACIONSOLICITUDCREDITO
WHERE TIPPRODUCTOSOLICITUDRBM = 'CC'
  AND DESCANALVENTARBMPER NOT IN ('YAPE', 'OTROS');


