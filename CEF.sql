   AND A.MATVENDEDOR = B.CODVENDEDORCOMERCIALBCAMINORISTA
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
),

-- =========================================================
-- 4) CASCADA LEGACY (gating)
-- =========================================================
MD_OK AS (
  SELECT DISTINCT CODSOLICITUD
  FROM (
    SELECT CODSOLICITUD FROM MATCH_MD_1A
    UNION ALL SELECT CODSOLICITUD FROM MATCH_MD_1B
    UNION ALL SELECT CODSOLICITUD FROM MATCH_MD_1C
    UNION ALL SELECT CODSOLICITUD FROM MATCH_MD_1D
    UNION ALL SELECT CODSOLICITUD FROM MATCH_MD_1E
    UNION ALL SELECT CODSOLICITUD FROM MATCH_MD_1F
    UNION ALL SELECT CODSOLICITUD FROM MATCH_MD_1G
    UNION ALL SELECT CODSOLICITUD FROM MATCH_MD_1H
    UNION ALL SELECT CODSOLICITUD FROM MATCH_MD_1I
  ) t
),

PEND_LGC_2A AS (
  SELECT A.*
  FROM TP_BASE A
  LEFT ANTI JOIN MD_OK M
    ON A.CODSOLICITUD = M.CODSOLICITUD
),

