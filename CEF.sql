WITH
BASE_TC_NUEVA AS (
  SELECT CODMES, CODSOLICITUD, CODCLAVECTA
  FROM CATALOG_LHCL_PROD_BCP_EXPL.BCP_EDV_RBMBDN.T72496_TP_FINAL_BASE
  WHERE CODCLAVECTA IS NOT NULL
),

M_TARJETACREDITO AS (
  SELECT CODCLAVECTA, MIN(FECACTIVACIONTARJETA) AS FECACTIVACIONTARJETA
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_TARJETACREDITO
  WHERE FECACTIVACIONTARJETA IS NOT NULL
  GROUP BY CODCLAVECTA
),

BASE_CUENTAS AS (
  SELECT DISTINCT CODCLAVECTA
  FROM BASE_TC_NUEVA
),

CUENTAS_USADAS AS (
  SELECT DISTINCT T.CODCLAVECTA
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_TRANSACCIONCUENTATARJETACREDITO T
  LEFT SEMI JOIN /*+ BROADCAST(B) */ BASE_CUENTAS B
    ON T.CODCLAVECTA = B.CODCLAVECTA
  WHERE T.FECTRX IS NOT NULL
),

TP_BASE AS (
  SELECT
    A.CODMES,
    A.CODSOLICITUD,
    A.CODCLAVECTA,
    B.FECACTIVACIONTARJETA,
    CASE WHEN B.CODCLAVECTA IS NOT NULL THEN 1 ELSE 0 END AS FLGTCACTIVADA,
    CASE WHEN B.CODCLAVECTA IS NOT NULL AND U.CODCLAVECTA IS NOT NULL THEN 1 ELSE 0 END AS FLGTCUSADA
  FROM BASE_TC_NUEVA A
  LEFT JOIN M_TARJETACREDITO B
    ON A.CODCLAVECTA = B.CODCLAVECTA
  LEFT JOIN CUENTAS_USADAS U
    ON A.CODCLAVECTA = U.CODCLAVECTA
)

SELECT
  CODMES,
  COUNT(1) AS CTDSOLICITUDESEMITIDAS,
  SUM(FLGTCACTIVADA) AS CTDSOLICITUDESACTIVADAS,
  SUM(FLGTCUSADA) AS CTDSOLICITUDESUSADAS
FROM TP_BASE
GROUP BY CODMES;
