WITH
BASE_VEHICULAR AS (
  SELECT
    A.CODMES,
    UPPER(TRIM(A.CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
    COALESCE(A.FLGCAMPANIA, 0) AS FLGLEAD,
    UPPER(TRIM(A.DECISIONANALISTA)) AS DECISIONANALISTA,
    A.MTOFINANCIADO,
    B.CODCLAVEPARTYCLI
  FROM CATALOG_LHCL_PROD_BCP_EXPL.BCP_EDV_RBMBDN.T72496_VEHICULAR A
  LEFT JOIN CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_CLIENTE B
    ON UPPER(TRIM(A.CODINTERNOCOMPUTACIONAL)) = UPPER(TRIM(B.CODINTERNOCOMPUTACIONAL))
  WHERE A.FLGDESEMBOLSO = 0
    AND A.DECISIONANALISTA IN ('APROBADO','RECHAZADO','DEVUELTO')
    AND A.CODMES BETWEEN 202508 AND 202510
),

CLIENTE_MES AS (
  SELECT
    CODMES,
    CODCLAVEPARTYCLI,

    MAX(CASE WHEN FLGLEAD = 1 AND DECISIONANALISTA = 'RECHAZADO' THEN 1 ELSE 0 END) AS FLG_LEAD_RECHAZADO,
    MAX(CASE WHEN FLGLEAD = 1 AND DECISIONANALISTA = 'APROBADO'  THEN 1 ELSE 0 END) AS FLG_LEAD_APROBADO,
    MAX(CASE WHEN FLGLEAD = 0 AND DECISIONANALISTA = 'RECHAZADO' THEN 1 ELSE 0 END) AS FLG_SINLEAD_RECHAZADO,
    MAX(CASE WHEN FLGLEAD = 0 AND DECISIONANALISTA = 'APROBADO'  THEN 1 ELSE 0 END) AS FLG_SINLEAD_APROBADO,
    MAX(CASE WHEN FLGLEAD = 1 AND DECISIONANALISTA = 'DEVUELTO'  THEN 1 ELSE 0 END) AS FLG_LEAD_DEVUELTO,
    MAX(CASE WHEN FLGLEAD = 0 AND DECISIONANALISTA = 'DEVUELTO'  THEN 1 ELSE 0 END) AS FLG_SINLEAD_DEVUELTO,
    MAX(COALESCE(MTOFINANCIADO,0)) AS MTOFINANCIADO_MAX
  FROM BASE_VEHICULAR
  WHERE CODCLAVEPARTYCLI IS NOT NULL
  GROUP BY CODMES, CODCLAVEPARTYCLI
),

SBS_DETALLE AS (
  SELECT
    CAST(date_format(FECDIA,'yyyyMM') AS INT) AS CODMES,
    CODCLAVEPARTYCLI,
    NBREMPSISTEMAFINANCIERO,
    FECDIA,
    TIPCREDITO,
    CTDDIAMOROSIDAD,
    DESTIPCREDITO,
    DESCTACONTABLESBS,
    DESTIPCLASIFRIESGO,
    DESTIPMONEDA,
    MTODEUDASOL,
    CASE
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('NORMAL') THEN 1
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('CON PROBLEMAS POTENCIALES(CPP)','CPP','CON PROBLEMAS POTENCIALES (CPP)') THEN 2
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('DEFICIENTE') THEN 3
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('DUDOSO') THEN 4
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('PERDIDA','PÉRDIDA') THEN 5
      ELSE 0
    END AS RIESGO_ORD
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_DEUDORSBSDETALLE
  WHERE UPPER(TRIM(NBREMPSISTEMAFINANCIERO)) NOT LIKE 'BANCO DE CREDITO DEL PER%'
    AND UPPER(TRIM(DESCTACONTABLESBS)) IN (
      'CRÉDITOS NO REVOLVENTES',
      'LÍNEAS NO REVOLVENTES',
      'OTROS CRÉDITOS DE CONSUMO',
      'DISPOSICIÓN DE EFECTIVO',
      'PRÉSTAMOS NO REVOLVENTES PARA AUTOMÓVILES',
      'PRÉSTAMOS NO REVOLVENTES PARA LIBRE DISPONIBILIDAD'
    )
    AND DESTIPCREDITO IN ('CREDITO DE CONSUMO NO REVOLVENTE')
),

/* desplazamos mes anterior al mes actual para comparar aparición */
SBS_ANTERIOR AS (
  SELECT
    CODCLAVEPARTYCLI,
    CAST(
      date_format(
        add_months(to_date(concat(CAST(CODMES AS STRING),'01'),'yyyyMMdd'),1),
        'yyyyMM'
      ) AS INT
    ) AS CODMES,
    NBREMPSISTEMAFINANCIERO,
    DESCTACONTABLESBS,
    DESTIPCREDITO
  FROM SBS_DETALLE
  GROUP BY
    CODCLAVEPARTYCLI,
    CAST(
      date_format(
        add_months(to_date(concat(CAST(CODMES AS STRING),'01'),'yyyyMMdd'),1),
        'yyyyMM'
      ) AS INT
    ),
    NBREMPSISTEMAFINANCIERO,
    DESCTACONTABLESBS,
    DESTIPCREDITO
),

/* línea nueva = no existía en mes previo para la misma combinación */
LINEA_NUEVA AS (
  SELECT
    a.*
  FROM SBS_DETALLE a
  LEFT JOIN SBS_ANTERIOR b
    ON a.CODCLAVEPARTYCLI       = b.CODCLAVEPARTYCLI
   AND a.CODMES                 = b.CODMES
   AND a.NBREMPSISTEMAFINANCIERO = b.NBREMPSISTEMAFINANCIERO
   AND a.DESCTACONTABLESBS      = b.DESCTACONTABLESBS
   AND a.DESTIPCREDITO          = b.DESTIPCREDITO
  WHERE b.CODCLAVEPARTYCLI IS NULL
),

/* solo líneas nuevas relevantes vs monto financiado */
CLIENTE_LINEA_FUGA AS (
  SELECT
    c.CODMES,
    c.CODCLAVEPARTYCLI,
    c.FLG_LEAD_RECHAZADO,
    c.FLG_LEAD_APROBADO,
    c.FLG_SINLEAD_RECHAZADO,
    c.FLG_SINLEAD_APROBADO,
    c.FLG_LEAD_DEVUELTO,
    c.FLG_SINLEAD_DEVUELTO,
    c.MTOFINANCIADO_MAX,

    l.NBREMPSISTEMAFINANCIERO,
    l.FECDIA,
    l.TIPCREDITO,
    l.CTDDIAMOROSIDAD,
    l.DESTIPCREDITO,
    l.DESCTACONTABLESBS,
    l.DESTIPCLASIFRIESGO,
    l.DESTIPMONEDA,
    l.MTODEUDASOL,
    l.RIESGO_ORD
  FROM CLIENTE_MES c
  INNER JOIN LINEA_NUEVA l
    ON c.CODCLAVEPARTYCLI = l.CODCLAVEPARTYCLI
   AND c.CODMES = l.CODMES
  WHERE c.MTOFINANCIADO_MAX > 0
    AND COALESCE(l.MTODEUDASOL,0) >= 0.5 * c.MTOFINANCIADO_MAX
    AND COALESCE(l.CTDDIAMOROSIDAD,0) = 0
),

/* volvemos a 1 fila por cliente-mes para no duplicar conteos */
CLIENTE_MES_FUGA AS (
  SELECT
    CODMES,
    CODCLAVEPARTYCLI,

    MAX(FLG_LEAD_RECHAZADO)    AS FLG_LEAD_RECHAZADO,
    MAX(FLG_LEAD_APROBADO)     AS FLG_LEAD_APROBADO,
    MAX(FLG_SINLEAD_RECHAZADO) AS FLG_SINLEAD_RECHAZADO,
    MAX(FLG_SINLEAD_APROBADO)  AS FLG_SINLEAD_APROBADO,
    MAX(FLG_LEAD_DEVUELTO)     AS FLG_LEAD_DEVUELTO,
    MAX(FLG_SINLEAD_DEVUELTO)  AS FLG_SINLEAD_DEVUELTO,

    MAX(MTOFINANCIADO_MAX) AS MTOFINANCIADO_MAX,
    SUM(MTODEUDASOL) AS MTO_FUGA_SBS,
    MAX(RIESGO_ORD) AS DESTIPCLASIFRIESGO_PEOR_ORD,
    CASE MAX(RIESGO_ORD)
      WHEN 1 THEN 'NORMAL'
      WHEN 2 THEN 'CON PROBLEMAS POTENCIALES(CPP)'
      WHEN 3 THEN 'DEFICIENTE'
      WHEN 4 THEN 'DUDOSO'
      WHEN 5 THEN 'PERDIDA'
      ELSE 'SIN INFO'
    END AS DESTIPCLASIFRIESGO_PEOR
  FROM CLIENTE_LINEA_FUGA
  GROUP BY CODMES, CODCLAVEPARTYCLI
),

AGG_CLIENTES AS (
  SELECT
    CODMES,
    DESTIPCLASIFRIESGO_PEOR,
    DESTIPCLASIFRIESGO_PEOR_ORD,

    COUNT(DISTINCT CASE WHEN FLG_LEAD_RECHAZADO    = 1 THEN CODCLAVEPARTYCLI END) AS CTD_LEAD_RECHAZADO,
    COUNT(DISTINCT CASE WHEN FLG_LEAD_APROBADO     = 1 THEN CODCLAVEPARTYCLI END) AS CTD_LEAD_APROBADO,
    COUNT(DISTINCT CASE WHEN FLG_SINLEAD_RECHAZADO = 1 THEN CODCLAVEPARTYCLI END) AS CTD_SINLEAD_RECHAZADO,
    COUNT(DISTINCT CASE WHEN FLG_SINLEAD_APROBADO  = 1 THEN CODCLAVEPARTYCLI END) AS CTD_SINLEAD_APROBADO,
    COUNT(DISTINCT CASE WHEN FLG_LEAD_DEVUELTO     = 1 THEN CODCLAVEPARTYCLI END) AS CTD_LEAD_DEVUELTO,
    COUNT(DISTINCT CASE WHEN FLG_SINLEAD_DEVUELTO  = 1 THEN CODCLAVEPARTYCLI END) AS CTD_SINLEAD_DEVUELTO,

    SUM(CASE WHEN FLG_LEAD_RECHAZADO    = 1 THEN MTO_FUGA_SBS ELSE 0 END) AS MTO_FUGA_SBS_LEAD_RECHAZADO,
    SUM(CASE WHEN FLG_LEAD_APROBADO     = 1 THEN MTO_FUGA_SBS ELSE 0 END) AS MTO_FUGA_SBS_LEAD_APROBADO,
    SUM(CASE WHEN FLG_SINLEAD_RECHAZADO = 1 THEN MTO_FUGA_SBS ELSE 0 END) AS MTO_FUGA_SBS_SINLEAD_RECHAZADO,
    SUM(CASE WHEN FLG_SINLEAD_APROBADO  = 1 THEN MTO_FUGA_SBS ELSE 0 END) AS MTO_FUGA_SBS_SINLEAD_APROBADO,
    SUM(CASE WHEN FLG_LEAD_DEVUELTO     = 1 THEN MTO_FUGA_SBS ELSE 0 END) AS MTO_FUGA_SBS_LEAD_DEVUELTO,
    SUM(CASE WHEN FLG_SINLEAD_DEVUELTO  = 1 THEN MTO_FUGA_SBS ELSE 0 END) AS MTO_FUGA_SBS_SINLEAD_DEVUELTO

  FROM CLIENTE_MES_FUGA
  GROUP BY CODMES, DESTIPCLASIFRIESGO_PEOR, DESTIPCLASIFRIESGO_PEOR_ORD
)

SELECT *
FROM AGG_CLIENTES
ORDER BY CODMES, DESTIPCLASIFRIESGO_PEOR_ORD;
