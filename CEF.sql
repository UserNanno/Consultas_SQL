WITH
LOGINS_BMO AS (  --- LOGINS TOTALES
	SELECT
		date_format(FECINTERACCION, 'yyyyMM') AS CODMES,
		FECINTERACCION,
		CODCLAVEPARTYCLI
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_INTERACCIONBANCAMOVIL
	WHERE FLGVALIDA = 'S' AND TIPINTERACCION = '300' AND FECRUTINA >= '2025-10-01'
),
LEADS_BMO AS ( -- LEADS DE BMO
	SELECT
		A.CODMESCAMPANIAANALISISMERCADO,
		A.CODCLAVEPARTYCLI,
		A.FLGVENTACANALENVIOCAMPANIA,
		A.MTOFINALVENTAEFECTIVASOL
	FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_CRM_FUNNELCONVERSION_VU.HD_CANALANALISISMERCADO A
	WHERE A.CODMESCAMPANIAANALISISMERCADO BETWEEN '202509' AND '202512' AND FECRUTINA IN ('2025-09-30', '2025-10-31', '2025-11-30', '2025-12-29')
		AND A.DESCANALLEAD = 'BANCA MOVIL' AND FLGENVIOCAMPANIACLI = '1'
),
LOGINS_LEADS_BMO AS (
	SELECT
		A.CODMES,
		A.FECINTERACCION,
		A.CODCLAVEPARTYCLI,
		(CASE
			WHEN B.CODCLAVEPARTYCLI IS NOT NULL THEN 1 ELSE 0
		END) AS FLGLEAD,
		B.FLGVENTACANALENVIOCAMPANIA,
		B.MTOFINALVENTAEFECTIVASOL
	FROM LOGINS_BMO A
	LEFT JOIN LEADS_BMO B ON (A.CODCLAVEPARTYCLI = B.CODCLAVEPARTYCLI AND A.CODMES = B.CODMESCAMPANIAANALISISMERCADO)
),
FUNNELCREDITO AS (
	SELECT
		CODMESCAMPANIAANALISISMERCADO,
		CODCLAVEPARTYCLI,
		(CASE
			WHEN A.TIPACCIONCOMERCIAL IN ('6', '110') AND A.TIPOFERTA IN ('0003','0038','0041','0098') THEN 'CEF'
			WHEN A.TIPACCIONCOMERCIAL IN ('17', '18') AND A.TIPOFERTA IN ('0003','0005','0123') THEN 'TC'
		END) AS PRODUCTO,
		FLGSOLICITUDCREDITO,
		FLGSOLICITUDCREDITOAPROBADO,
		MTOFINALOFERTASOL
	FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_CRM_FUNNELCONVERSION_VU.HD_PRODUCTOANALISISMERCADO
	WHERE CODMESCAMPANIAANALISISMERCADO BETWEEN '202509' AND '202512'
	AND TIPACCIONCOMERCIAL IN ('6', '110', '17', '18') AND TIPOFERTA IN ('0003','0038','0041','0098', '0005', '0123')
	AND FECRUTINA IN ('2025-09-30', '2025-10-31', '2025-11-30', '2025-12-30')
),
LOGINS_LEADS_BMO_FUNNELCREDITO AS (
	SELECT
		A.CODMES,
		A.FECINTERACCION,
		A.CODCLAVEPARTYCLI,
		A.FLGLEAD,
		B.PRODUCTO,
		B.FLGSOLICITUDCREDITO,
		B.FLGSOLICITUDCREDITOAPROBADO,
		B.MTOFINALOFERTASOL,
		A.FLGVENTACANALENVIOCAMPANIA,
		A.MTOFINALVENTAEFECTIVASOL
	FROM LOGINS_LEADS_BMO A
	LEFT JOIN FUNNELCREDITO B ON (A.CODCLAVEPARTYCLI = B.CODCLAVEPARTYCLI AND A.CODMES = B.CODMESCAMPANIAANALISISMERCADO)
)
SELECT
	CODMES,
	FECINTERACCION,
	PRODUCTO,
	COUNT(1) AS CTDCLIENTES_LOGIN,
	SUM(CASE WHEN FLGLEAD = 1 THEN 1 ELSE 0 END) AS CTDCLIENTES_LOGIN_LEAD,
	SUM(CASE WHEN FLGLEAD = 0 THEN 1 ELSE 0 END) AS CTDCLIENTES_LOGIN_SIN_LEAD,
	SUM(CASE WHEN FLGLEAD = 1 AND THEN 1 FLGSOLICITUDCREDITO = 1 ELSE 0 END) AS CTDCLIENTES_LOGIN_LEAD_SOLICITUD,
	SUM(CASE WHEN FLGLEAD = 1 AND THEN 1 FLGSOLICITUDCREDITO = 1 AND FLGSOLICITUDCREDITOAPROBADO = 1 ELSE 0 END) AS CTDCLIENTES_LOGIN_LEAD_SOLICITUD_APROBADA,
	SUM(CASE WHEN FLGLEAD = 1 AND THEN 1 FLGSOLICITUDCREDITO = 1 AND FLGSOLICITUDCREDITOAPROBADO = 1 AND FLGVENTACANALENVIOCAMPANIA = 1 ELSE 0 END) AS CTDCLIENTES_LOGIN_LEAD_SOLICITUD_APROBADA_VENTA
FROM LOGINS_LEADS_BMO_FUNNELCREDITO
GROUP BY CODMES, FECINTERACCION, PRODUCTO;
