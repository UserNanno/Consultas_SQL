      B.CODCLAVECTA,
      B.CODCLAVETARJETA,
      'MD_1G_SIN_SOL_CLIENTE_MONTO' AS ORIGEN,
      ROW_NUMBER() OVER (
        PARTITION BY A.CODSOLICITUD
        ORDER BY B.FECVENTABCAMINORISTA ASC
      ) AS rn_pick
    FROM PEND_MD_1G A
    JOIN MD_VENTACOMISIONABLEBANCAMINORISTA_NOAPP B
      ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
     AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
     AND B.FECVENTABCAMINORISTA IS NOT NULL
     AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
  ) t
  WHERE rn_pick = 1
),

PEND_MD_1H AS (
  SELECT A.*
  FROM PEND_MD_1G A
  LEFT ANTI JOIN MATCH_MD_1G M
    ON A.CODSOLICITUD = M.CODSOLICITUD
),

-- 1H: fecventa NULL (ALL APPS)
MATCH_MD_1H AS (
  SELECT
    CODMES,CODSOLICITUD,DESPRODUCTO,
    MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,
    CODCLAVECTA,CODCLAVETARJETA,ORIGEN
  FROM (
    SELECT
      A.CODMES,
      A.CODSOLICITUD,
      A.DESPRODUCTO,
      A.MTODESEMBOLSADO AS MTOLINEACREDITO,
      A.FECDESEMBOLSO AS FECEMISIONTARJETA,
      CAST(NULL AS DATE) AS FECVENTATARJETA,
      B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
      B.CODCLAVECTA,
      B.CODCLAVETARJETA,
      'MD_1H_FECVENTA_NULL' AS ORIGEN,
      ROW_NUMBER() OVER (
        PARTITION BY A.CODSOLICITUD
        ORDER BY COALESCE(B.CODCLAVETARJETA,''), COALESCE(B.CODCLAVECTA,'')
      ) AS rn_pick
    FROM PEND_MD_1H A
    JOIN MD_VENTACOMISIONABLEBANCAMINORISTA_NOAPP B
      ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
     AND A.CODSOLICITUD = B.CODSOLICITUD
     AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
     AND B.FECVENTABCAMINORISTA IS NULL
  ) t
  WHERE rn_pick = 1
),

PEND_MD_1I AS (
  SELECT A.*
  FROM PEND_MD_1H A
  LEFT ANTI JOIN MATCH_MD_1H M
    ON A.CODSOLICITUD = M.CODSOLICITUD
),

-- 1I: COE, sin condición por fecventa (sol+cic+vend+monto)
MATCH_MD_1I AS (
  SELECT
    A.CODMES,
    A.CODSOLICITUD,
    A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
