WITH
LOGINS AS (
  SELECT
    date_format(FECINTERACCION, 'yyyyMM') AS CODMES,
    to_date(FECINTERACCION) AS FECHA,
    CODCLAVEPARTYCLI
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_INTERACCIONBANCAMOVIL
  WHERE FLGVALIDA = 'S'
    AND TIPINTERACCION = '300'
    AND FECRUTINA >= '2025-09-01' AND FECRUTINA <= '2025-12-31'
  GROUP BY date_format(FECINTERACCION, 'yyyyMM'), to_date(FECINTERACCION), CODCLAVEPARTYCLI
),
FIRST_LOGIN_M AS (
  SELECT CODMES, CODCLAVEPARTYCLI, MIN(FECHA) AS FIRST_LOGIN_MES
  FROM LOGINS
  GROUP BY CODMES, CODCLAVEPARTYCLI
),
DIAS_MES AS (
  SELECT DISTINCT CODMES, FECHA
  FROM LOGINS
),

CANAL AS (
  SELECT
    CODMESCAMPANIAANALISISMERCADO,
    CODCLAVEPARTYCLI,
    TIPACCIONCOMERCIAL,
    TIPOFERTA,
    UPPER(TRIM(DESTIPACCIONCOMERCIAL)) AS DESTIPACCIONCOMERCIAL,
    FLGENVIOCAMPANIACLI,
    FLGVENTACANALENVIOCAMPANIA,
    MTOFINALVENTAEFECTIVASOL
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_CRM_FUNNELCONVERSION_VU.HD_CANALANALISISMERCADO
  WHERE CODMESCAMPANIAANALISISMERCADO BETWEEN '202509' AND '202512'
    AND TIPACCIONCOMERCIAL IN ('6', '110', '17', '18')
    AND TIPOFERTA IN ('0003','0038','0041','0098', '0005', '0123')
    AND FECRUTINA IN ('2025-09-30', '2025-10-31', '2025-11-30', '2025-12-29')
    AND DESCANALLEAD = 'BANCA MOVIL'
  -- OJO: recomiendo NO filtrar FLGENVIOCAMPANIACLI='1'
),
PRODUCTO AS (
  SELECT
    CODMESCAMPANIAANALISISMERCADO,
    CODCLAVEPARTYCLI,
    TIPACCIONCOMERCIAL,
    TIPOFERTA,
    UPPER(TRIM(DESTIPACCIONCOMERCIAL)) AS DESTIPACCIONCOMERCIAL,
    FLGSOLICITUDCREDITO,
    FLGSOLICITUDCREDITOAPROBADO,
    MTOFINALOFERTASOL
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_CRM_FUNNELCONVERSION_VU.HD_PRODUCTOANALISISMERCADO
  WHERE CODMESCAMPANIAANALISISMERCADO BETWEEN '202509' AND '202512'
    AND TIPACCIONCOMERCIAL IN ('6', '110', '17', '18')
    AND TIPOFERTA IN ('0003','0038','0041','0098', '0005', '0123')
    AND FECRUTINA IN ('2025-09-30', '2025-10-31', '2025-11-30', '2025-12-30')
),

DIM_OFER AS (
  SELECT
    A.CODMESCAMPANIAANALISISMERCADO AS CODMES,
    A.CODCLAVEPARTYCLI,
    A.TIPACCIONCOMERCIAL,
    A.TIPOFERTA,
    A.DESTIPACCIONCOMERCIAL,
    A.FLGSOLICITUDCREDITO,
    A.FLGSOLICITUDCREDITOAPROBADO,
    A.MTOFINALOFERTASOL,
    COALESCE(B.FLGENVIOCAMPANIACLI,'0') AS FLGENVIOCAMPANIACLI,
    COALESCE(B.FLGVENTACANALENVIOCAMPANIA,'0') AS FLGVENTACANALENVIOCAMPANIA,
    B.MTOFINALVENTAEFECTIVASOL
  FROM PRODUCTO A
  LEFT JOIN CANAL B
    ON  A.CODMESCAMPANIAANALISISMERCADO = B.CODMESCAMPANIAANALISISMERCADO
    AND A.CODCLAVEPARTYCLI = B.CODCLAVEPARTYCLI
    AND A.TIPACCIONCOMERCIAL = B.TIPACCIONCOMERCIAL
    AND A.TIPOFERTA = B.TIPOFERTA
),

-- Para cada cliente-mes (primer login), “etiquétalo” con si tenía alguna oferta con lead/solicitud/etc.
DIM_MES_CLIENTE AS (
  SELECT
    CODMES,
    CODCLAVEPARTYCLI,
    MAX(CASE WHEN FLGENVIOCAMPANIACLI = '1' THEN 1 ELSE 0 END) AS FLGLEAD_BMO,
    MAX(CASE WHEN FLGVENTACANALENVIOCAMPANIA = '1' THEN 1 ELSE 0 END) AS FLGVENTA_BMO,
    MAX(CASE WHEN FLGSOLICITUDCREDITO = 1 THEN 1 ELSE 0 END) AS FLGSOL,
    MAX(CASE WHEN FLGSOLICITUDCREDITOAPROBADO = 1 THEN 1 ELSE 0 END) AS FLGAPR
  FROM DIM_OFER
  GROUP BY CODMES, CODCLAVEPARTYCLI
),

-- Incrementos por día (cada cliente entra 1 vez en su primer login del mes)
INC_DIARIO AS (
  SELECT
    F.CODMES,
    F.FIRST_LOGIN_MES AS FECHA,
    COUNT(*) AS INC_LOGIN,
    SUM(CASE WHEN COALESCE(D.FLGLEAD_BMO,0)=1 THEN 1 ELSE 0 END) AS INC_LOGIN_LEAD,
    SUM(CASE WHEN COALESCE(D.FLGLEAD_BMO,0)=0 THEN 1 ELSE 0 END) AS INC_LOGIN_SIN_LEAD,
    SUM(CASE WHEN COALESCE(D.FLGLEAD_BMO,0)=1 AND COALESCE(D.FLGSOL,0)=1 THEN 1 ELSE 0 END) AS INC_LEAD_SOL,
    SUM(CASE WHEN COALESCE(D.FLGLEAD_BMO,0)=1 AND COALESCE(D.FLGSOL,0)=1 AND COALESCE(D.FLGAPR,0)=1 THEN 1 ELSE 0 END) AS INC_LEAD_SOL_APR,
    SUM(CASE WHEN COALESCE(D.FLGLEAD_BMO,0)=1 AND COALESCE(D.FLGSOL,0)=1 AND COALESCE(D.FLGAPR,0)=1 AND COALESCE(D.FLGVENTA_BMO,0)=1 THEN 1 ELSE 0 END) AS INC_LEAD_SOL_APR_VTA
  FROM FIRST_LOGIN_M F
  LEFT JOIN DIM_MES_CLIENTE D
    ON D.CODMES = F.CODMES AND D.CODCLAVEPARTYCLI = F.CODCLAVEPARTYCLI
  GROUP BY F.CODMES, F.FIRST_LOGIN_MES
)

SELECT
  D.CODMES,
  D.FECHA AS FECINTERACCION,

  SUM(COALESCE(I.INC_LOGIN,0)) OVER (PARTITION BY D.CODMES ORDER BY D.FECHA) AS ACUM_LOGIN,
  SUM(COALESCE(I.INC_LOGIN_LEAD,0)) OVER (PARTITION BY D.CODMES ORDER BY D.FECHA) AS ACUM_LOGIN_LEAD,
  SUM(COALESCE(I.INC_LOGIN_SIN_LEAD,0)) OVER (PARTITION BY D.CODMES ORDER BY D.FECHA) AS ACUM_LOGIN_SIN_LEAD,
  SUM(COALESCE(I.INC_LEAD_SOL,0)) OVER (PARTITION BY D.CODMES ORDER BY D.FECHA) AS ACUM_LEAD_SOL,
  SUM(COALESCE(I.INC_LEAD_SOL_APR,0)) OVER (PARTITION BY D.CODMES ORDER BY D.FECHA) AS ACUM_LEAD_SOL_APR,
  SUM(COALESCE(I.INC_LEAD_SOL_APR_VTA,0)) OVER (PARTITION BY D.CODMES ORDER BY D.FECHA) AS ACUM_LEAD_SOL_APR_VTA
FROM DIAS_MES D
LEFT JOIN INC_DIARIO I
  ON D.CODMES = I.CODMES AND D.FECHA = I.FECHA
ORDER BY D.CODMES, D.FECHA;
