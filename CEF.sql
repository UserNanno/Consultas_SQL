La razón por la que debes hacer el flujo manual es porque los reportes PDF de Equifax no están estructurados de forma uniforme y presentan variaciones en:
Formato de tablas (algunas están partidas en varias páginas).
Glosas y productos (pueden aparecer con nombres incompletos o mezclados).
Períodos (no siempre están en el orden requerido).
Datos faltantes o ilegibles (OCR puede generar errores).
El proceso automático exige cumplir reglas estrictas (productos permitidos, glosas exactas, períodos específicos, sumas correctas), y si alguna condición falla, el sistema debe abortar para evitar errores en la extracción.
Por eso, cuando el reporte no cumple la estructura esperada, se marca como CASO NO AUTOMATIZABLE — REQUIERE PROCESO MANUAL para garantizar trazabilidad y exactitud.
 
