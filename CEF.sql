WITH
BASE_VEHICULAR AS (
    SELECT
        A.CODMES,
        UPPER(TRIM(A.CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
        COALESCE(A.FLGCAMPANIA, 0) AS FLGLEAD,
        UPPER(TRIM(A.DECISIONANALISTA)) AS DECISIONANALISTA,
        A.MTOFINANCIADO,
        B.CODCLAVEPARTYCLI
    FROM CATALOG_LHCL_PROD_BCP_EXPL.BCP_EDV_RBMBDN.T72496_VEHICULAR A
    LEFT JOIN CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_CLIENTE B
        ON UPPER(TRIM(A.CODINTERNOCOMPUTACIONAL)) = UPPER(TRIM(B.CODINTERNOCOMPUTACIONAL))
    WHERE A.FLGDESEMBOLSO = 0
      AND A.DECISIONANALISTA IN ('APROBADO','RECHAZADO','DEVUELTO')
      AND A.CODMES BETWEEN 202508 AND 202510
),

CLIENTE_MES AS (
    SELECT
        CODMES,
        CODCLAVEPARTYCLI,
        MAX(COALESCE(MTOFINANCIADO,0)) AS MTOFINANCIADO_MAX,

        MAX(CASE WHEN FLGLEAD = 1 AND DECISIONANALISTA = 'APROBADO'  THEN 1 ELSE 0 END) AS FLG_LEAD_APROBADO,
        MAX(CASE WHEN FLGLEAD = 0 AND DECISIONANALISTA = 'APROBADO'  THEN 1 ELSE 0 END) AS FLG_SINLEAD_APROBADO,

        MAX(CASE WHEN FLGLEAD = 1 AND DECISIONANALISTA = 'RECHAZADO' THEN 1 ELSE 0 END) AS FLG_LEAD_RECHAZADO,
        MAX(CASE WHEN FLGLEAD = 0 AND DECISIONANALISTA = 'RECHAZADO' THEN 1 ELSE 0 END) AS FLG_SINLEAD_RECHAZADO,

        MAX(CASE WHEN FLGLEAD = 1 AND DECISIONANALISTA = 'DEVUELTO'  THEN 1 ELSE 0 END) AS FLG_LEAD_DEVUELTO,
        MAX(CASE WHEN FLGLEAD = 0 AND DECISIONANALISTA = 'DEVUELTO'  THEN 1 ELSE 0 END) AS FLG_SINLEAD_DEVUELTO
    FROM BASE_VEHICULAR
    WHERE CODCLAVEPARTYCLI IS NOT NULL
    GROUP BY CODMES, CODCLAVEPARTYCLI
),

SBS AS (
    SELECT
        CAST(date_format(FECDIA,'yyyyMM') AS INT) AS CODMES,
        CODCLAVEPARTYCLI,
        NBREMPSISTEMAFINANCIERO,
        DESTIPCREDITO,
        DESCTACONTABLESBS,
        DESTIPCLASIFRIESGO,
        CTDDIAMOROSIDAD,
        MTODEUDASOL
    FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_DEUDORSBSDETALLE
    WHERE UPPER(TRIM(NBREMPSISTEMAFINANCIERO)) NOT LIKE 'BANCO DE CREDITO DEL PER%'
      AND UPPER(TRIM(DESCTACONTABLESBS)) IN (
        'CRÉDITOS NO REVOLVENTES',
        'PRÉSTAMOS ASOCIADOS A LÍNEAS REVOLVENTES',
        'OTROS CRÉDITOS DE CONSUMO',
        'DISPOSICIÓN DE EFECTIVO',
        'PRÉSTAMOS NO REVOLVENTES PARA AUTOMÓVILES',
        'PRÉSTAMOS NO REVOLVENTES PARA LIBRE DISPONIBILIDAD'
      )
),

SBS_ACTUAL AS (
    SELECT *
    FROM SBS
),

SBS_ANTERIOR AS (
    SELECT
        CODCLAVEPARTYCLI,
        CAST(
            date_format(
                add_months(to_date(concat(CAST(CODMES AS STRING),'01'),'yyyyMMdd'),1),
                'yyyyMM'
            ) AS INT
        ) AS CODMES,
        NBREMPSISTEMAFINANCIERO,
        MTODEUDASOL
    FROM SBS
),

LINEA_NUEVA AS (
    SELECT
        a.*
    FROM SBS_ACTUAL a
    LEFT JOIN SBS_ANTERIOR b
        ON a.CODCLAVEPARTYCLI = b.CODCLAVEPARTYCLI
       AND a.CODMES = b.CODMES
       AND a.NBREMPSISTEMAFINANCIERO = b.NBREMPSISTEMAFINANCIERO
    WHERE b.CODCLAVEPARTYCLI IS NULL
),

CLIENTE_FUGA AS (
    SELECT
        c.CODMES,
        c.CODCLAVEPARTYCLI,
        MAX(l.MTODEUDASOL) AS MTOFUGASBS
    FROM CLIENTE_MES c
    INNER JOIN LINEA_NUEVA l
        ON c.CODCLAVEPARTYCLI = l.CODCLAVEPARTYCLI
       AND c.CODMES = l.CODMES
    WHERE c.MTOFINANCIADO_MAX > 0
      AND l.MTODEUDASOL >= 0.5 * c.MTOFINANCIADO_MAX
      AND COALESCE(l.CTDDIAMOROSIDAD,0) = 0
    GROUP BY
        c.CODMES,
        c.CODCLAVEPARTYCLI
),

BASE_FINAL AS (
    SELECT
        c.*,
        CASE WHEN f.CODCLAVEPARTYCLI IS NOT NULL THEN 1 ELSE 0 END AS FLGFUGASBS,
        COALESCE(f.MTOFUGASBS,0) AS MTOFUGASBS
    FROM CLIENTE_MES c
    LEFT JOIN CLIENTE_FUGA f
        ON c.CODMES = f.CODMES
       AND c.CODCLAVEPARTYCLI = f.CODCLAVEPARTYCLI
),

AGG_FUNNEL AS (
    SELECT
        CODMES,

        COUNT(DISTINCT CASE WHEN FLG_LEAD_APROBADO = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESLEADAPROBADONODESEMBOLSADO,
        COUNT(DISTINCT CASE WHEN FLG_SINLEAD_APROBADO = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESSINLEADAPROBADONODESEMBOLSADO,

        COUNT(DISTINCT CASE WHEN FLG_LEAD_RECHAZADO = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESLEADRECHAZADONODESEMBOLSADO,
        COUNT(DISTINCT CASE WHEN FLG_SINLEAD_RECHAZADO = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESSINLEADRECHAZADONODESEMBOLSADO,

        COUNT(DISTINCT CASE WHEN FLG_LEAD_DEVUELTO = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESLEADDEVUELTONODESEMBOLSADO,
        COUNT(DISTINCT CASE WHEN FLG_SINLEAD_DEVUELTO = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESSINLEADDEVUELTONODESEMBOLSADO,

        COUNT(DISTINCT CASE WHEN FLG_LEAD_APROBADO = 1 AND FLGFUGASBS = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESLEADAPROBADONODESEMBOLSADOFUGASBS,
        COUNT(DISTINCT CASE WHEN FLG_SINLEAD_APROBADO = 1 AND FLGFUGASBS = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESSINLEADAPROBADONODESEMBOLSADOFUGASBS,

        COUNT(DISTINCT CASE WHEN FLG_LEAD_RECHAZADO = 1 AND FLGFUGASBS = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESLEADRECHAZADONODESEMBOLSADOFUGASBS,
        COUNT(DISTINCT CASE WHEN FLG_SINLEAD_RECHAZADO = 1 AND FLGFUGASBS = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESSINLEADRECHAZADONODESEMBOLSADOFUGASBS,

        COUNT(DISTINCT CASE WHEN FLG_LEAD_DEVUELTO = 1 AND FLGFUGASBS = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESLEADDEVUELTONODESEMBOLSADOFUGASBS,
        COUNT(DISTINCT CASE WHEN FLG_SINLEAD_DEVUELTO = 1 AND FLGFUGASBS = 1 THEN CODCLAVEPARTYCLI END) AS CTDCLIENTESSINLEADDEVUELTONODESEMBOLSADOFUGASBS,

        SUM(CASE WHEN FLG_LEAD_APROBADO = 1 AND FLGFUGASBS = 1 THEN MTOFUGASBS ELSE 0 END) AS MTOLEADAPROBADONODESEMBOLSADOFUGASBS,
        SUM(CASE WHEN FLG_SINLEAD_APROBADO = 1 AND FLGFUGASBS = 1 THEN MTOFUGASBS ELSE 0 END) AS MTOSINLEADAPROBADONODESEMBOLSADOFUGASBS,

        SUM(CASE WHEN FLG_LEAD_RECHAZADO = 1 AND FLGFUGASBS = 1 THEN MTOFUGASBS ELSE 0 END) AS MTOLEADRECHAZADONODESEMBOLSADOFUGASBS,
        SUM(CASE WHEN FLG_SINLEAD_RECHAZADO = 1 AND FLGFUGASBS = 1 THEN MTOFUGASBS ELSE 0 END) AS MTOSINLEADRECHAZADONODESEMBOLSADOFUGASBS,

        SUM(CASE WHEN FLG_LEAD_DEVUELTO = 1 AND FLGFUGASBS = 1 THEN MTOFUGASBS ELSE 0 END) AS MTOLEADDEVUELTONODESEMBOLSADOFUGASBS,
        SUM(CASE WHEN FLG_SINLEAD_DEVUELTO = 1 AND FLGFUGASBS = 1 THEN MTOFUGASBS ELSE 0 END) AS MTOSINLEADDEVUELTONODESEMBOLSADOFUGASBS

    FROM BASE_FINAL
    GROUP BY CODMES
)

SELECT *
FROM AGG_FUNNEL
ORDER BY CODMES;
