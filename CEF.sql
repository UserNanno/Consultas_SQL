WITH
BASE_VEHICULAR AS (
  SELECT
    A.CODMES,
    A.NUMOPERACION,
    UPPER(TRIM(A.CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
    COALESCE(A.FLGCAMPANIA, 0) AS FLGCAMPANIA,
    UPPER(TRIM(A.DECISIONANALISTA)) AS DECISIONANALISTA,
    A.MTOFINANCIADO,
    B.CODCLAVEPARTYCLI
  FROM CATALOG_LHCL_PROD_BCP_EXPL.BCP_EDV_RBMBDN.T72496_VEHICULAR A
  LEFT JOIN CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_CLIENTE B
    ON UPPER(TRIM(A.CODINTERNOCOMPUTACIONAL)) = UPPER(TRIM(B.CODINTERNOCOMPUTACIONAL))
  WHERE A.FLGDESEMBOLSO = 0
    AND A.DECISIONANALISTA IN ('APROBADO','RECHAZADO','DEVUELTO')
    AND A.CODMES BETWEEN 202508 AND 202510
),

CLIENTE_MES AS (
  SELECT
    CODMES,
    CODCLAVEPARTYCLI,
    MAX(CASE WHEN DECISIONANALISTA = 'RECHAZADO' AND FLGCAMPANIA = 1 THEN 1 ELSE 0 END) AS FLG_RECHAZADO_CON_LEAD,
    MAX(CASE WHEN DECISIONANALISTA = 'RECHAZADO' AND FLGCAMPANIA = 0 THEN 1 ELSE 0 END) AS FLG_RECHAZADO_SIN_LEAD,
    MAX(CASE WHEN DECISIONANALISTA = 'APROBADO'  AND FLGCAMPANIA = 1 THEN 1 ELSE 0 END) AS FLG_APROBADO_CON_LEAD,
    MAX(CASE WHEN DECISIONANALISTA = 'APROBADO'  AND FLGCAMPANIA = 0 THEN 1 ELSE 0 END) AS FLG_APROBADO_SIN_LEAD,
    MAX(CASE WHEN DECISIONANALISTA = 'DEVUELTO'  AND FLGCAMPANIA = 1 THEN 1 ELSE 0 END) AS FLG_DEVUELTO_CON_LEAD,
    MAX(CASE WHEN DECISIONANALISTA = 'DEVUELTO'  AND FLGCAMPANIA = 0 THEN 1 ELSE 0 END) AS FLG_DEVUELTO_SIN_LEAD,
    MAX(COALESCE(MTOFINANCIADO,0)) AS MTO_FIN_MAX
  FROM BASE_VEHICULAR
  WHERE CODCLAVEPARTYCLI IS NOT NULL
  GROUP BY CODMES, CODCLAVEPARTYCLI
),

SBS_DETALLE AS (
  SELECT
    CAST(date_format(FECDIA,'yyyyMM') AS INT) AS CODMESDEUDA,
    CODCLAVEPARTYCLI,
    FECDIA,
    TIPCREDITO,
    CTDDIAMOROSIDAD,
    DESTIPCREDITO,
    DESCTACONTABLESBS,
    NBREMPSISTEMAFINANCIERO,
    DESTIPCLASIFRIESGO,
    DESTIPMONEDA,
    MTODEUDASOL,
    CASE
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('NORMAL') THEN 1
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('CON PROBLEMAS POTENCIALES(CPP)','CPP','CON PROBLEMAS POTENCIALES (CPP)') THEN 2
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('DEFICIENTE') THEN 3
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('DUDOSO') THEN 4
      WHEN UPPER(TRIM(DESTIPCLASIFRIESGO)) IN ('PERDIDA','PÉRDIDA') THEN 5
      ELSE 0
    END AS RIESGO_ORD
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_DEUDORSBSDETALLE
  WHERE UPPER(TRIM(NBREMPSISTEMAFINANCIERO)) NOT LIKE 'BANCO DE CREDITO DEL PER%'
    AND UPPER(TRIM(DESCTACONTABLESBS)) IN (
      'CRÉDITOS NO REVOLVENTES',
      'LÍNEAS NO REVOLVENTES',
      'OTROS CRÉDITOS DE CONSUMO',
      'DISPOSICIÓN DE EFECTIVO',
      'PRÉSTAMOS NO REVOLVENTES PARA AUTOMÓVILES',
      'PRÉSTAMOS NO REVOLVENTES PARA LIBRE DISPONIBILIDAD'
    )
    AND DESTIPCREDITO IN ('CREDITO DE CONSUMO NO REVOLVENTE')
),

/* catálogo de líneas existentes en el mes previo, desplazadas al mes actual */
SBS_PREV_KEY AS (
  SELECT
    CAST(
      date_format(
        add_months(to_date(concat(CAST(CODMESDEUDA AS STRING),'01'),'yyyyMMdd'),1),
        'yyyyMM'
      ) AS INT
    ) AS CODMESDEUDA,
    CODCLAVEPARTYCLI,
    NBREMPSISTEMAFINANCIERO,
    DESCTACONTABLESBS,
    DESTIPCREDITO
  FROM SBS_DETALLE
  GROUP BY
    CAST(
      date_format(
        add_months(to_date(concat(CAST(CODMESDEUDA AS STRING),'01'),'yyyyMMdd'),1),
        'yyyyMM'
      ) AS INT
    ),
    CODCLAVEPARTYCLI,
    NBREMPSISTEMAFINANCIERO,
    DESCTACONTABLESBS,
    DESTIPCREDITO
),

/* líneas nuevas en el mes actual */
SBS_LINEA_NUEVA AS (
  SELECT
    a.*
  FROM SBS_DETALLE a
  LEFT JOIN SBS_PREV_KEY b
    ON a.CODMESDEUDA            = b.CODMESDEUDA
   AND a.CODCLAVEPARTYCLI       = b.CODCLAVEPARTYCLI
   AND a.NBREMPSISTEMAFINANCIERO = b.NBREMPSISTEMAFINANCIERO
   AND a.DESCTACONTABLESBS      = b.DESCTACONTABLESBS
   AND a.DESTIPCREDITO          = b.DESTIPCREDITO
  WHERE b.CODCLAVEPARTYCLI IS NULL
),

/* resumen cliente-mes, igual que en tu lógica previa */
CLIENTE_MES_SBS AS (
  SELECT
    c.*,

    MAX(CASE
      WHEN n.CTDDIAMOROSIDAD = 0
       AND COALESCE(n.MTODEUDASOL,0) >= 0.50 * c.MTO_FIN_MAX
      THEN 1 ELSE 0
    END) AS FLG_FUGA_SBS_50PCT,

    MAX(CASE
      WHEN n.CTDDIAMOROSIDAD = 0
       AND COALESCE(n.MTODEUDASOL,0) >= 0.50 * c.MTO_FIN_MAX
      THEN COALESCE(n.MTODEUDASOL,0) ELSE 0
    END) AS MTO_FUGA_SBS_50PCT,

    MAX(CASE
      WHEN n.CTDDIAMOROSIDAD = 0
       AND COALESCE(n.MTODEUDASOL,0) >= 0.50 * c.MTO_FIN_MAX
      THEN COALESCE(n.RIESGO_ORD,0) ELSE 0
    END) AS DESTIPCLASIFRIESGO_PEOR_ORD

  FROM CLIENTE_MES c
  LEFT JOIN SBS_LINEA_NUEVA n
    ON c.CODCLAVEPARTYCLI = n.CODCLAVEPARTYCLI
   AND c.CODMES = n.CODMESDEUDA
  GROUP BY
    c.CODMES,
    c.CODCLAVEPARTYCLI,
    c.FLG_RECHAZADO_CON_LEAD,
    c.FLG_RECHAZADO_SIN_LEAD,
    c.FLG_APROBADO_CON_LEAD,
    c.FLG_APROBADO_SIN_LEAD,
    c.FLG_DEVUELTO_CON_LEAD,
    c.FLG_DEVUELTO_SIN_LEAD,
    c.MTO_FIN_MAX
),

CLIENTE_MES_SBS_2 AS (
  SELECT
    *,
    CASE DESTIPCLASIFRIESGO_PEOR_ORD
      WHEN 1 THEN 'NORMAL'
      WHEN 2 THEN 'CON PROBLEMAS POTENCIALES(CPP)'
      WHEN 3 THEN 'DEFICIENTE'
      WHEN 4 THEN 'DUDOSO'
      WHEN 5 THEN 'PERDIDA'
      ELSE 'SIN INFO'
    END AS DESTIPCLASIFRIESGO_PEOR
  FROM CLIENTE_MES_SBS
),

AGG_CLIENTES AS (
  SELECT
    CODMES,
    DESTIPCLASIFRIESGO_PEOR,
    DESTIPCLASIFRIESGO_PEOR_ORD,

    COUNT(DISTINCT CASE WHEN FLG_RECHAZADO_CON_LEAD = 1 THEN CODCLAVEPARTYCLI END) AS CTD_RECHAZADO_CON_LEAD,
    COUNT(DISTINCT CASE WHEN FLG_RECHAZADO_SIN_LEAD = 1 THEN CODCLAVEPARTYCLI END) AS CTD_RECHAZADO_SIN_LEAD,
    COUNT(DISTINCT CASE WHEN FLG_APROBADO_CON_LEAD = 1 THEN CODCLAVEPARTYCLI END) AS CTD_APROBADO_CON_LEAD,
    COUNT(DISTINCT CASE WHEN FLG_APROBADO_SIN_LEAD = 1 THEN CODCLAVEPARTYCLI END) AS CTD_APROBADO_SIN_LEAD,
    COUNT(DISTINCT CASE WHEN FLG_DEVUELTO_CON_LEAD = 1 THEN CODCLAVEPARTYCLI END) AS CTD_DEVUELTO_CON_LEAD,
    COUNT(DISTINCT CASE WHEN FLG_DEVUELTO_SIN_LEAD = 1 THEN CODCLAVEPARTYCLI END) AS CTD_DEVUELTO_SIN_LEAD,

    COUNT(DISTINCT CASE
      WHEN FLG_RECHAZADO_CON_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN CODCLAVEPARTYCLI END) AS CTD_RECHAZADO_CON_LEAD_FUGA_SBS_50PCT,

    COUNT(DISTINCT CASE
      WHEN FLG_RECHAZADO_SIN_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN CODCLAVEPARTYCLI END) AS CTD_RECHAZADO_SIN_LEAD_FUGA_SBS_50PCT,

    COUNT(DISTINCT CASE
      WHEN FLG_APROBADO_CON_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN CODCLAVEPARTYCLI END) AS CTD_APROBADO_CON_LEAD_FUGA_SBS_50PCT,

    COUNT(DISTINCT CASE
      WHEN FLG_APROBADO_SIN_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN CODCLAVEPARTYCLI END) AS CTD_APROBADO_SIN_LEAD_FUGA_SBS_50PCT,

    COUNT(DISTINCT CASE
      WHEN FLG_DEVUELTO_CON_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN CODCLAVEPARTYCLI END) AS CTD_DEVUELTO_CON_LEAD_FUGA_SBS_50PCT,

    COUNT(DISTINCT CASE
      WHEN FLG_DEVUELTO_SIN_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN CODCLAVEPARTYCLI END) AS CTD_DEVUELTO_SIN_LEAD_FUGA_SBS_50PCT,

    SUM(CASE
      WHEN FLG_RECHAZADO_CON_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN MTO_FUGA_SBS_50PCT ELSE 0 END) AS MTO_RECHAZADO_CON_LEAD_FUGA_SBS_50PCT,

    SUM(CASE
      WHEN FLG_RECHAZADO_SIN_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN MTO_FUGA_SBS_50PCT ELSE 0 END) AS MTO_RECHAZADO_SIN_LEAD_FUGA_SBS_50PCT,

    SUM(CASE
      WHEN FLG_APROBADO_CON_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN MTO_FUGA_SBS_50PCT ELSE 0 END) AS MTO_APROBADO_CON_LEAD_FUGA_SBS_50PCT,

    SUM(CASE
      WHEN FLG_APROBADO_SIN_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN MTO_FUGA_SBS_50PCT ELSE 0 END) AS MTO_APROBADO_SIN_LEAD_FUGA_SBS_50PCT,

    SUM(CASE
      WHEN FLG_DEVUELTO_CON_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN MTO_FUGA_SBS_50PCT ELSE 0 END) AS MTO_DEVUELTO_CON_LEAD_FUGA_SBS_50PCT,

    SUM(CASE
      WHEN FLG_DEVUELTO_SIN_LEAD = 1 AND MTO_FIN_MAX > 0 AND FLG_FUGA_SBS_50PCT = 1
      THEN MTO_FUGA_SBS_50PCT ELSE 0 END) AS MTO_DEVUELTO_SIN_LEAD_FUGA_SBS_50PCT

  FROM CLIENTE_MES_SBS_2
  GROUP BY CODMES, DESTIPCLASIFRIESGO_PEOR, DESTIPCLASIFRIESGO_PEOR_ORD
)

SELECT *
FROM AGG_CLIENTES
ORDER BY CODMES, DESTIPCLASIFRIESGO_PEOR_ORD;
