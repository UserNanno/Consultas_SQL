  CODMES,
  date_format(FECDESEMBOLSO, 'yyyyMM') AS CODMES_DESEMBOLSO,
  DESTIPOPERACION,
  COUNT(*) AS cnt
FROM CATALOG_LHCL_PROD_BCP_EXPL.BCP_EDV_RBMBDN.T72496_MIGRACION_PLANCHON
