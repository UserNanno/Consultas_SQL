WITH
BASE_TC_NUEVA AS (
	SELECT
		CODMES,
		CODSOLICITUD,
		CODCLAVECTA
	FROM CATALOG_LHCL_PROD_BCP_EXPL.BCP_EDV_RBMBDN.T72496_TP_FINAL_BASE
),
M_TARJETACREDITO AS (
	SELECT
		CODCLAVECTA,
		MIN(FECACTIVACIONTARJETA) AS FECACTIVACIONTARJETA
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_TARJETACREDITO
	WHERE FECACTIVACIONTARJETA IS NOT NULL
	GROUP BY CODCLAVECTA
),
H_TRANSACCIONCUENTATARJETACREDITO AS (
	SELECT
		CODCLAVECTA
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_TRANSACCIONCUENTATARJETACREDITO
	WHERE FECTRX IS NOT NULL
),
TP_BASE AS (
	SELECT
		A.CODMES,
		A.CODSOLICITUD,
		A.CODCLAVECTA,
		(CASE WHEN B.CODCLAVECTA IS NOT NULL THEN B.FECACTIVACIONTARJETA ELSE NULL END) AS FECACTIVACIONTARJETA,
		(CASE WHEN B.CODCLAVECTA IS NOT NULL THEN 1 ELSE 0 END) AS FLGTCACTIVADA,
		(CASE WHEN B.CODCLAVECTA IS NOT NULL AND C.CODCLAVECTA IS NOT NULL THEN 1 ELSE 0 END) AS FLGTCUSADA
	FROM BASE_TC_NUEVA A
	LEFT JOIN M_TARJETACREDITO B ON (A.CODCLAVECTA = B.CODCLAVECTA)
	LEFT JOIN H_TRANSACCIONCUENTATARJETACREDITO C ON (A.CODCLAVECTA = C.CODCLAVECTA)
)
SELECT
	CODMES,
	COUNT(1) AS CTDSOLICITUDESEMITIDAS,
	SUM(FLGTCACTIVADA) AS CTDSOLICITUDESACTIVADAS,
	SUM(FLGTCUSADA) AS CTDSOLICITUDESUSADAS
FROM TP_BASE
GROUP BY CODMES;
