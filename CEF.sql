WITH
-- =========================
-- Base
-- =========================
HM_SOLICITUDES_SCRM AS (
  SELECT
    CODMES,
    CODSOLICITUD,
    CODINTERNOCOMPUTACIONAL,
    UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO,
    MATVENDEDOR,
    FECSOLICITUD,
    FECDESEMBOLSO,
    MTODESEMBOLSADO
  FROM CATALOG_LHCL_PROD_BCP.BCP_EDV_RBMBDN.T72496_HM_SOLICITUDES_SCRM
  WHERE CENTROATENCION = 'CENTRALIZADO'
    AND DESTIPOPERACION = 'TARJETA CREDITO NUEVA'
    AND FLGDESEMBOLSADO = 1
),
M_PRODUCTOTIOAUX AS (
  SELECT CODPRODUCTO, UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_PRODUCTOTIOAUX
),
TP_BASE AS (
  SELECT
    A.CODMES,
    A.CODSOLICITUD,
    A.FECSOLICITUD,
    A.CODINTERNOCOMPUTACIONAL,
    A.DESPRODUCTO,
    A.MATVENDEDOR,
    B.CODPRODUCTO,
    A.FECDESEMBOLSO,
    A.MTODESEMBOLSADO
  FROM HM_SOLICITUDES_SCRM A
  LEFT JOIN M_PRODUCTOTIOAUX B
    ON A.DESPRODUCTO = B.DESPRODUCTO
),

-- =========================
-- MD (dos versiones: con filtro COE y sin filtro COE)
-- =========================
MD_VENTA_COE AS (
  SELECT
    CODSOLICITUD,
    CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    CODPRODUCTO,
    CODCLAVECTA,
    CODCLAVETARJETA
    -- Si existe vendedor en MD, agrégalo aquí:
    -- , CODVENDEDORBCAMINORISTA AS CODVENDEDOR_MD
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.MD_VENTACOMISIONABLEBANCAMINORISTA
  WHERE DESAPPVENTABCAMINORISTA = 'COE SALESFORCE'
    AND DESTIPPRODUCTOBCAMINORISTA = 'TARJETA DE CREDITO'
    AND UPPER(TRIM(DESPRODUCTOCOMERCIALBCAMINORISTA)) LIKE '%VENTA%'
),
MD_VENTA_ALLAPPS AS (
  SELECT
    CODSOLICITUD,
    CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    CODPRODUCTO,
    CODCLAVECTA,
    CODCLAVETARJETA
    -- , CODVENDEDORBCAMINORISTA AS CODVENDEDOR_MD
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.MD_VENTACOMISIONABLEBANCAMINORISTA
  WHERE DESTIPPRODUCTOBCAMINORISTA = 'TARJETA DE CREDITO'
    AND UPPER(TRIM(DESPRODUCTOCOMERCIALBCAMINORISTA)) LIKE '%VENTA%'
),

-- =========================
-- LEGACY (dos versiones: con filtro producto y sin filtro producto)
-- =========================
HE_LGC_FILTRADA AS (
  SELECT
    TRIM(CODUNICOVENTABCAMINORISTALEGACY) AS CODSOLICITUD,
    CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    CODVENDEDORBCAMINORISTALEGACY,
    CODCLAVECTA
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.HE_VENTACOMISIONABLEBANCAMINORISTALEGACY
  WHERE CODPRODUCTOCOMERCIALBCAMINORISTALEGACY IN ('17', '18')
),
HE_LGC_TODOS_PROD AS (
  SELECT
    TRIM(CODUNICOVENTABCAMINORISTALEGACY) AS CODSOLICITUD,
    CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    CODVENDEDORBCAMINORISTALEGACY,
    CODCLAVECTA
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.HE_VENTACOMISIONABLEBANCAMINORISTALEGACY
),

-- =========================================================
-- 1A: MD estricto (COE + solicitud + producto + monto + ventana)
-- =========================================================
MD_1A AS (
  SELECT
    A.CODMES, A.CODSOLICITUD, A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA, B.CODCLAVETARJETA,
    'MD_1A' AS ORIGEN,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.CODPRODUCTO, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM TP_BASE A
  JOIN MD_VENTA_COE B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.CODSOLICITUD = B.CODSOLICITUD
   AND A.CODPRODUCTO = B.CODPRODUCTO
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
MD_1A_1 AS (SELECT * FROM MD_1A WHERE RN=1),

PEND_1B AS (
  SELECT A.*
  FROM TP_BASE A
  LEFT JOIN MD_1A_1 M ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

-- =========================
-- 1B: MD sin producto
-- =========================
MD_1B AS (
  SELECT
    A.CODMES, A.CODSOLICITUD, A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA, B.CODCLAVETARJETA,
    'MD_1B_SIN_PROD' AS ORIGEN,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_1B A
  JOIN MD_VENTA_COE B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.CODSOLICITUD = B.CODSOLICITUD
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
MD_1B_1 AS (SELECT * FROM MD_1B WHERE RN=1),

PEND_1D AS (
  SELECT A.*
  FROM PEND_1B A
  LEFT JOIN MD_1B_1 M ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

-- =========================================================
-- 1D: MD sin CODSOLICITUD (mantiene COE, producto y monto)
-- =========================================================
MD_1D AS (
  SELECT
    A.CODMES, A.CODSOLICITUD, A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA, B.CODCLAVETARJETA,
    'MD_1D_SIN_SOL' AS ORIGEN,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.CODPRODUCTO, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_1D A
  JOIN MD_VENTA_COE B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.CODPRODUCTO = B.CODPRODUCTO
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
MD_1D_1 AS (SELECT * FROM MD_1D WHERE RN=1),

PEND_1E AS (
  SELECT A.*
  FROM PEND_1D A
  LEFT JOIN MD_1D_1 M ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

-- =========================================================
-- 1E: MD sin monto (con tolerancia)
--     Ajusta el umbral (ej: <= 1 sol o <= 0.5)
-- =========================================================
MD_1E AS (
  SELECT
    A.CODMES, A.CODSOLICITUD, A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA, B.CODCLAVETARJETA,
    'MD_1E_SIN_MONTO' AS ORIGEN,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD
      ORDER BY ABS(A.MTODESEMBOLSADO - B.MTOVENTABCAMINORISTA) ASC,
               B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_1E A
  JOIN MD_VENTA_COE B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.CODPRODUCTO = B.CODPRODUCTO
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
   AND ABS(A.MTODESEMBOLSADO - B.MTOVENTABCAMINORISTA) <= 1
),
MD_1E_1 AS (SELECT * FROM MD_1E WHERE RN=1),

PEND_1F AS (
  SELECT A.*
  FROM PEND_1E A
  LEFT JOIN MD_1E_1 M ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

-- =========================================================
-- 1F: MD sin solicitud + sin vendedor + sin COE (usa MD_VENTA_ALLAPPS)
--     (Aquí NO uso monto ni solicitud; rankeo por monto+fecha)
-- =========================================================
MD_1F AS (
  SELECT
    A.CODMES, A.CODSOLICITUD, A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA, B.CODCLAVETARJETA,
    'MD_1F_SUPER_FUZZY' AS ORIGEN,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD
      ORDER BY ABS(A.MTODESEMBOLSADO - B.MTOVENTABCAMINORISTA) ASC,
               B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_1F A
  JOIN MD_VENTA_ALLAPPS B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.CODPRODUCTO = B.CODPRODUCTO
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
MD_1F_1 AS (SELECT * FROM MD_1F WHERE RN=1),

-- =========================
-- Cola para LEGACY (lo que no matcheó en ningún MD)
-- =========================
PEND_LGC AS (
  SELECT A.*
  FROM TP_BASE A
  LEFT JOIN (
    SELECT CODSOLICITUD FROM MD_1A_1
    UNION ALL SELECT CODSOLICITUD FROM MD_1B_1
    UNION ALL SELECT CODSOLICITUD FROM MD_1D_1
    UNION ALL SELECT CODSOLICITUD FROM MD_1E_1
    UNION ALL SELECT CODSOLICITUD FROM MD_1F_1
  ) X ON A.CODSOLICITUD = X.CODSOLICITUD
  WHERE X.CODSOLICITUD IS NULL
),

-- =========================
-- 2A: LEGACY estricto (con filtro prod + con solicitud + con vendedor)
-- =========================
LGC_2A AS (
  SELECT
    A.CODMES, A.CODSOLICITUD, A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA,
    CAST(NULL AS STRING) AS CODCLAVETARJETA,
    'LGC_2A' AS ORIGEN,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_LGC A
  JOIN HE_LGC_FILTRADA B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.MATVENDEDOR = B.CODVENDEDORBCAMINORISTALEGACY
   AND A.CODSOLICITUD = B.CODSOLICITUD
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
LGC_2A_1 AS (SELECT * FROM LGC_2A WHERE RN=1),

PEND_2B AS (
  SELECT A.*
  FROM PEND_LGC A
  LEFT JOIN LGC_2A_1 M ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

-- =========================
-- 2B: LEGACY sin CODSOLICITUD (con vendedor)
-- =========================
LGC_2B AS (
  SELECT
    A.CODMES, A.CODSOLICITUD, A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA,
    CAST(NULL AS STRING) AS CODCLAVETARJETA,
    'LGC_2B_SIN_SOL' AS ORIGEN,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_2B A
  JOIN HE_LGC_FILTRADA B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.MATVENDEDOR = B.CODVENDEDORBCAMINORISTALEGACY
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
LGC_2B_1 AS (SELECT * FROM LGC_2B WHERE RN=1),

PEND_2C AS (
  SELECT A.*
  FROM PEND_2B A
  LEFT JOIN LGC_2B_1 M ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

-- =========================
-- 2C: LEGACY quitando MATVENDEDOR (pero con CODSOLICITUD)
-- =========================
LGC_2C AS (
  SELECT
    A.CODMES, A.CODSOLICITUD, A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA,
    CAST(NULL AS STRING) AS CODCLAVETARJETA,
    'LGC_2C_SIN_VENDEDOR' AS ORIGEN,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_2C A
  JOIN HE_LGC_FILTRADA B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.CODSOLICITUD = B.CODSOLICITUD
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
LGC_2C_1 AS (SELECT * FROM LGC_2C WHERE RN=1),

PEND_2D AS (
  SELECT A.*
  FROM PEND_2C A
  LEFT JOIN LGC_2C_1 M ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

-- =========================
-- 2D: LEGACY quitando filtro de producto (usa HE_LGC_TODOS_PROD)
--     (aquí mantengo vendedor+solicitud; si quieres, puedes también replicar 2B/2C sobre todos los prod)
-- =========================
LGC_2D AS (
  SELECT
    A.CODMES, A.CODSOLICITUD, A.DESPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA,
    CAST(NULL AS STRING) AS CODCLAVETARJETA,
    'LGC_2D_SIN_FILTRO_PROD' AS ORIGEN,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_2D A
  JOIN HE_LGC_TODOS_PROD B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.MATVENDEDOR = B.CODVENDEDORBCAMINORISTALEGACY
   AND A.CODSOLICITUD = B.CODSOLICITUD
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
LGC_2D_1 AS (SELECT * FROM LGC_2D WHERE RN=1),

-- =========================
-- UNION final (solo RN=1)
-- =========================
BASE_UNION AS (
  SELECT CODMES,CODSOLICITUD,DESPRODUCTO,MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,CODCLAVECTA,CODCLAVETARJETA,ORIGEN
  FROM MD_1A_1
  UNION ALL SELECT CODMES,CODSOLICITUD,DESPRODUCTO,MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,CODCLAVECTA,CODCLAVETARJETA,ORIGEN FROM MD_1B_1
  UNION ALL SELECT CODMES,CODSOLICITUD,DESPRODUCTO,MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,CODCLAVECTA,CODCLAVETARJETA,ORIGEN FROM MD_1D_1
  UNION ALL SELECT CODMES,CODSOLICITUD,DESPRODUCTO,MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,CODCLAVECTA,CODCLAVETARJETA,ORIGEN FROM MD_1E_1
  UNION ALL SELECT CODMES,CODSOLICITUD,DESPRODUCTO,MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,CODCLAVECTA,CODCLAVETARJETA,ORIGEN FROM MD_1F_1
  UNION ALL SELECT CODMES,CODSOLICITUD,DESPRODUCTO,MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,CODCLAVECTA,CODCLAVETARJETA,ORIGEN FROM LGC_2A_1
  UNION ALL SELECT CODMES,CODSOLICITUD,DESPRODUCTO,MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,CODCLAVECTA,CODCLAVETARJETA,ORIGEN FROM LGC_2B_1
  UNION ALL SELECT CODMES,CODSOLICITUD,DESPRODUCTO,MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,CODCLAVECTA,CODCLAVETARJETA,ORIGEN FROM LGC_2C_1
  UNION ALL SELECT CODMES,CODSOLICITUD,DESPRODUCTO,MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,CODCLAVECTA,CODCLAVETARJETA,ORIGEN FROM LGC_2D_1
)
SELECT * FROM BASE_UNION;
