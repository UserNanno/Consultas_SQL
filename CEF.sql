WITH
LOGINS AS (
  SELECT
    date_format(FECINTERACCION, 'yyyyMM') AS CODMES,
    to_date(FECINTERACCION) AS FECHA,
    CODCLAVEPARTYCLI
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_INTERACCIONBANCAMOVIL
  WHERE FLGVALIDA = 'S'
    AND TIPINTERACCION = '300'
    AND FECRUTINA >= '2025-09-01' AND FECRUTINA <= '2025-10-01'
  GROUP BY date_format(FECINTERACCION, 'yyyyMM'), to_date(FECINTERACCION), CODCLAVEPARTYCLI
),

FIRST_LOGIN_M AS (
  SELECT
    CODMES,
    CODCLAVEPARTYCLI,
    MIN(FECHA) AS FIRST_LOGIN_MES
  FROM LOGINS
  GROUP BY CODMES, CODCLAVEPARTYCLI
),

DIAS_MES AS (
  SELECT DISTINCT CODMES, FECHA
  FROM LOGINS
),

CANAL AS (
  SELECT
    CODMESCAMPANIAANALISISMERCADO AS CODMES,
    CODCLAVEPARTYCLI,
    UPPER(TRIM(DESABREVIACIONACCIONCOMERCIAL)) AS ACCION,
    MAX(CASE WHEN FLGENVIOCAMPANIACLI = '1' THEN 1 ELSE 0 END) AS FLGLEAD,
    MAX(CASE WHEN FLGVENTACANALENVIOCAMPANIA = '1' THEN 1 ELSE 0 END) AS FLGVENTA,
    MAX(MTOFINALVENTAEFECTIVASOL) AS MTOFINALVENTAEFECTIVASOL
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_CRM_FUNNELCONVERSION_VU.HD_CANALANALISISMERCADO
  WHERE CODMESCAMPANIAANALISISMERCADO BETWEEN '202509' AND '202512'
    AND FECRUTINA IN ('2025-09-30', '2025-10-31', '2025-11-30', '2025-12-29')
    AND DESCANALLEAD = 'BANCA MOVIL'
    AND FLGENVIOCAMPANIACLI = '1'
  GROUP BY CODMESCAMPANIAANALISISMERCADO, CODCLAVEPARTYCLI, UPPER(TRIM(DESABREVIACIONACCIONCOMERCIAL))
),

PRODUCTO AS (
  SELECT
    CODMESCAMPANIAANALISISMERCADO AS CODMES,
    CODCLAVEPARTYCLI,
    UPPER(TRIM(DESTIPACCIONCOMERCIAL)) AS ACCION,
    MAX(CASE WHEN FLGSOLICITUDCREDITO = 1 THEN 1 ELSE 0 END) AS FLGSOLICITUDCREDITO,
    MAX(CASE WHEN FLGSOLICITUDCREDITOAPROBADO = 1 THEN 1 ELSE 0 END) AS FLGSOLICITUDCREDITOAPROBADO,
    MAX(MTOFINALOFERTASOL) AS MTOFINALOFERTASOL
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_CRM_FUNNELCONVERSION_VU.HD_PRODUCTOANALISISMERCADO
  WHERE CODMESCAMPANIAANALISISMERCADO BETWEEN '202509' AND '202512'
    AND TIPACCIONCOMERCIAL IN ('6', '110', '17', '18')
    AND TIPOFERTA IN ('0003','0038','0041','0098', '0005', '0123')
  GROUP BY CODMESCAMPANIAANALISISMERCADO, CODCLAVEPARTYCLI, UPPER(TRIM(DESTIPACCIONCOMERCIAL))
),

DIM_PROD_CANAL AS (
  SELECT
    P.CODMES,
    P.CODCLAVEPARTYCLI,
    P.ACCION AS PRODUCTO,               -- aquí ACCION=CEF/TC
    COALESCE(C.FLGLEAD,0) AS FLGLEAD,
    COALESCE(C.FLGVENTA,0) AS FLGVENTA,
    P.FLGSOLICITUDCREDITO,
    P.FLGSOLICITUDCREDITOAPROBADO
  FROM PRODUCTO P
  LEFT JOIN CANAL C
    ON  C.CODMES = P.CODMES
    AND C.CODCLAVEPARTYCLI = P.CODCLAVEPARTYCLI
    AND C.ACCION = P.ACCION
),

-- 1 fila por cliente-mes-producto con su primer login del mes
FIRST_LOGIN_DIM AS (
  SELECT
    F.CODMES,
    F.FIRST_LOGIN_MES AS FECHA,
    D.PRODUCTO,
    F.CODCLAVEPARTYCLI,
    D.FLGLEAD,
    D.FLGVENTA,
    D.FLGSOLICITUDCREDITO,
    D.FLGSOLICITUDCREDITOAPROBADO
  FROM FIRST_LOGIN_M F
  LEFT JOIN DIM_PROD_CANAL D
    ON D.CODMES = F.CODMES AND D.CODCLAVEPARTYCLI = F.CODCLAVEPARTYCLI
),

-- incrementos por día (cada cliente aporta 1 en su primer día)
INC_DIARIO AS (
  SELECT
    CODMES,
    FECHA,
    PRODUCTO,
    COUNT(DISTINCT CODCLAVEPARTYCLI) AS INC_LOGIN,
    COUNT(DISTINCT CASE WHEN FLGLEAD = 1 THEN CODCLAVEPARTYCLI END) AS INC_LOGIN_LEAD,
    COUNT(DISTINCT CASE WHEN FLGLEAD = 1 AND FLGSOLICITUDCREDITO = 1 THEN CODCLAVEPARTYCLI END) AS INC_LEAD_SOL,
    COUNT(DISTINCT CASE WHEN FLGLEAD = 1 AND FLGSOLICITUDCREDITO = 1 AND FLGSOLICITUDCREDITOAPROBADO = 1 THEN CODCLAVEPARTYCLI END) AS INC_LEAD_SOL_APR,
    COUNT(DISTINCT CASE WHEN FLGLEAD = 1 AND FLGSOLICITUDCREDITO = 1 AND FLGSOLICITUDCREDITOAPROBADO = 1 AND FLGVENTA = 1 THEN CODCLAVEPARTYCLI END) AS INC_LEAD_SOL_APR_VTA
  FROM FIRST_LOGIN_DIM
  GROUP BY CODMES, FECHA, PRODUCTO
)

SELECT
  D.CODMES,
  D.FECHA AS FECINTERACCION,
  I.PRODUCTO,

  SUM(COALESCE(I.INC_LOGIN,0)) OVER (PARTITION BY D.CODMES, I.PRODUCTO ORDER BY D.FECHA ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS ACUM_LOGIN,
  SUM(COALESCE(I.INC_LOGIN_LEAD,0)) OVER (PARTITION BY D.CODMES, I.PRODUCTO ORDER BY D.FECHA ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS ACUM_LOGIN_LEAD,
  SUM(COALESCE(I.INC_LEAD_SOL,0)) OVER (PARTITION BY D.CODMES, I.PRODUCTO ORDER BY D.FECHA ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS ACUM_LEAD_SOL,
  SUM(COALESCE(I.INC_LEAD_SOL_APR,0)) OVER (PARTITION BY D.CODMES, I.PRODUCTO ORDER BY D.FECHA ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS ACUM_LEAD_SOL_APR,
  SUM(COALESCE(I.INC_LEAD_SOL_APR_VTA,0)) OVER (PARTITION BY D.CODMES, I.PRODUCTO ORDER BY D.FECHA ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS ACUM_LEAD_SOL_APR_VTA

FROM DIAS_MES D
LEFT JOIN INC_DIARIO I
  ON D.CODMES = I.CODMES AND D.FECHA = I.FECHA
ORDER BY D.CODMES, D.FECHA, I.PRODUCTO;
