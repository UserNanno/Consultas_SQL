WITH
LOGINS AS (  --- LOGINS TOTALES
	SELECT
		date_format(FECINTERACCION, 'yyyyMM') AS CODMES,
		FECINTERACCION,
		CODCLAVEPARTYCLI
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_INTERACCIONBANCAMOVIL
	WHERE FLGVALIDA = 'S' AND TIPINTERACCION = '300' AND FECRUTINA >= '2025-09-01' AND FECRUTINA <= '2025-12-31'
),
FIRST_LOGIN_M AS (
	SELECT
		CODMES,
		CODCLAVEPARTYCLI,
		MIN(FECINTERACCION) AS FEC_FIRST_LOGIN_MES
	FROM	LOGINS
	GROUP BY CODMES, CODCLAVEPARTYCLI
),
INCREMENTO_DIARIO AS (
	SELECT
		CODMES,
		FEC_FIRST_LOGIN_MES,
		COUNT(1) AS INC_CLIENTES_LOGIN
	FROM FIRST_LOGIN_M
	GROUP BY CODMES, FEC_FIRST_LOGIN_MES
),
DIAS_MES AS (
	SELECT
		DISTINCT
		CODMES,
		FECINTERACCION
	FROM LOGINS
),
ACUM_LOGINS AS (
	SELECT
		A.CODMES,
		A.FECINTERACCION,
		SUM(COALESCE(INC_CLIENTES_LOGIN,0))
		OVER (PARTITION BY A.CODMES ORDER BY A.FECINTERACCION ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS ACUM_CLIENTES_LOGIN
	FROM DIAS_MES A
	LEFT JOIN INCREMENTO_DIARIO B ON (A.CODMES = B.CODMES AND A.FECINTERACCION = B.FEC_FIRST_LOGIN_MES)
),
CANAL AS (
  SELECT
    CODMESCAMPANIAANALISISMERCADO,
    CODCLAVEPARTYCLI,
    TIPACCIONCOMERCIAL,
    TIPOFERTA,
    UPPER(TRIM(DESTIPACCIONCOMERCIAL)) AS DESTIPACCIONCOMERCIAL,
    FLGENVIOCAMPANIACLI,
    FLGVENTACANALENVIOCAMPANIA,
    MTOFINALVENTAEFECTIVASOL
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_CRM_FUNNELCONVERSION_VU.HD_CANALANALISISMERCADO
  WHERE CODMESCAMPANIAANALISISMERCADO BETWEEN '202509' AND '202512'
    AND TIPACCIONCOMERCIAL IN ('6', '110', '17', '18') AND TIPOFERTA IN ('0003','0038','0041','0098', '0005', '0123')
    AND FECRUTINA IN ('2025-09-30', '2025-10-31', '2025-11-30', '2025-12-29')
    AND DESCANALLEAD = 'BANCA MOVIL'
    AND FLGENVIOCAMPANIACLI='1'
),
PRODUCTO AS (
  SELECT
    CODMESCAMPANIAANALISISMERCADO,
    CODCLAVEPARTYCLI,
    TIPACCIONCOMERCIAL,
    TIPOFERTA,
    UPPER(TRIM(DESTIPACCIONCOMERCIAL)) AS DESTIPACCIONCOMERCIAL,
    FLGSOLICITUDCREDITO,
    FLGSOLICITUDCREDITOAPROBADO,
    MTOFINALOFERTASOL
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_CRM_FUNNELCONVERSION_VU.HD_PRODUCTOANALISISMERCADO
  WHERE CODMESCAMPANIAANALISISMERCADO BETWEEN '202509' AND '202512'
  AND TIPACCIONCOMERCIAL IN ('6', '110', '17', '18') AND TIPOFERTA IN ('0003','0038','0041','0098', '0005', '0123')
  AND FECRUTINA IN ('2025-09-30', '2025-10-31', '2025-11-30', '2025-12-30')
 
)
SELECT
  A.CODMESCAMPANIAANALISISMERCADO,
  A.CODCLAVEPARTYCLI,
  A.TIPACCIONCOMERCIAL,
  A.TIPOFERTA,
  A.DESTIPACCIONCOMERCIAL,
  A.FLGSOLICITUDCREDITO,
  A.FLGSOLICITUDCREDITOAPROBADO,
  A.MTOFINALOFERTASOL,
  B.FLGENVIOCAMPANIACLI,
  B.FLGVENTACANALENVIOCAMPANIA,
  B.MTOFINALVENTAEFECTIVASOL
FROM PRODUCTO A
JOIN CANAL B ON (A.CODMESCAMPANIAANALISISMERCADO = B.CODMESCAMPANIAANALISISMERCADO AND A.CODCLAVEPARTYCLI = B.CODCLAVEPARTYCLI AND A.TIPACCIONCOMERCIAL= B.TIPACCIONCOMERCIAL AND A.TIPOFERTA = B.TIPOFERTA)
