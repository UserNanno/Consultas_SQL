WITH
HM_SOLICITUDES_SCRM AS (
  SELECT
    CODMES,
    CODSOLICITUD,
    CODINTERNOCOMPUTACIONAL,
    UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO,
    MATVENDEDOR,
    FECSOLICITUD,
    FECDESEMBOLSO,
    MTODESEMBOLSADO
  FROM CATALOG_LHCL_PROD_BCP.BCP_EDV_RBMBDN.T72496_HM_SOLICITUDES_SCRM
  WHERE CENTROATENCION = 'CENTRALIZADO'
    AND DESTIPOPERACION = 'TARJETA CREDITO NUEVA'
    AND FLGDESEMBOLSADO = 1
),
M_PRODUCTOTIOAUX AS (
  SELECT
    CODPRODUCTO,
    UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_PRODUCTOTIOAUX
),
TP_BASE AS (
  SELECT
    A.CODMES,
    A.CODSOLICITUD,
    A.FECSOLICITUD,
    A.CODINTERNOCOMPUTACIONAL,
    A.DESPRODUCTO,
    A.MATVENDEDOR,
    B.CODPRODUCTO,
    A.FECDESEMBOLSO,
    A.MTODESEMBOLSADO
  FROM HM_SOLICITUDES_SCRM A
  LEFT JOIN M_PRODUCTOTIOAUX B
    ON A.DESPRODUCTO = B.DESPRODUCTO
),

MD_VENTACOMISIONABLEBANCAMINORISTA AS (
  SELECT
    CODSOLICITUD,
    CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    CODPRODUCTO,
    CODCLAVECTA,
    CODCLAVETARJETA
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.MD_VENTACOMISIONABLEBANCAMINORISTA
  WHERE DESAPPVENTABCAMINORISTA = 'COE SALESFORCE'
    AND DESTIPPRODUCTOBCAMINORISTA = 'TARJETA DE CREDITO'
    AND UPPER(TRIM(DESPRODUCTOCOMERCIALBCAMINORISTA)) LIKE '%VENTA%'
),

HE_VENTACOMISIONABLEBANCAMINORISTALEGACY AS (
  SELECT
    TRIM(CODUNICOVENTABCAMINORISTALEGACY) AS CODSOLICITUD,
    CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    CODVENDEDORBCAMINORISTALEGACY,
    CODCLAVECTA
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.HE_VENTACOMISIONABLEBANCAMINORISTALEGACY
  WHERE CODPRODUCTOCOMERCIALBCAMINORISTALEGACY IN ('17', '18')
),

/* =========================
   1) MATCH MD (con producto)
   ========================= */
MATCHED_MD AS (
  SELECT
    A.CODMES,
    A.CODSOLICITUD,
    A.DESPRODUCTO,
    A.MATVENDEDOR,
    A.CODINTERNOCOMPUTACIONAL,
    A.CODPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA,
    B.CODCLAVETARJETA,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.CODPRODUCTO, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM TP_BASE A
  JOIN MD_VENTACOMISIONABLEBANCAMINORISTA B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.CODPRODUCTO = B.CODPRODUCTO
   AND A.CODSOLICITUD = B.CODSOLICITUD
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
MATCHED_MD_1 AS (
  SELECT * FROM MATCHED_MD WHERE RN = 1
),

/* Cola: lo que NO matcheó en MD_1 */
PEND_MD AS (
  SELECT A.*
  FROM TP_BASE A
  LEFT JOIN MATCHED_MD_1 M
    ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

/* ===================================
   1B) MATCH MD (SIN CODPRODUCTO)
   todo igual excepto el producto
   =================================== */
MATCHED_MD_NOPROD AS (
  SELECT
    A.CODMES,
    A.CODSOLICITUD,
    A.DESPRODUCTO,
    A.MATVENDEDOR,
    A.CODINTERNOCOMPUTACIONAL,
    A.CODPRODUCTO,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA,
    B.CODCLAVETARJETA,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_MD A
  JOIN MD_VENTACOMISIONABLEBANCAMINORISTA B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.CODSOLICITUD = B.CODSOLICITUD
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
MATCHED_MD_NOPROD_1 AS (
  SELECT * FROM MATCHED_MD_NOPROD WHERE RN = 1
),

/* Cola: lo que no matcheó ni en MD_1 ni en MD_NOPROD_1 */
PEND_AFTER_MD AS (
  SELECT A.*
  FROM PEND_MD A
  LEFT JOIN MATCHED_MD_NOPROD_1 M
    ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

/* ===========================
   2) MATCH LEGACY (con solicitud)
   =========================== */
MATCHED_LGC AS (
  SELECT
    A.CODMES,
    A.CODSOLICITUD,
    A.DESPRODUCTO,
    A.MATVENDEDOR,
    A.CODINTERNOCOMPUTACIONAL,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_AFTER_MD A
  JOIN HE_VENTACOMISIONABLEBANCAMINORISTALEGACY B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.MATVENDEDOR = B.CODVENDEDORBCAMINORISTALEGACY
   AND A.CODSOLICITUD = B.CODSOLICITUD
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
MATCHED_LGC_1 AS (
  SELECT * FROM MATCHED_LGC WHERE RN = 1
),

/* Cola: lo que no matcheó en LEGACY con CODSOLICITUD */
PEND_AFTER_LGC AS (
  SELECT A.*
  FROM PEND_AFTER_MD A
  LEFT JOIN MATCHED_LGC_1 M
    ON A.CODSOLICITUD = M.CODSOLICITUD
  WHERE M.CODSOLICITUD IS NULL
),

/* ===================================
   2B) MATCH LEGACY SIN CODSOLICITUD
   (calza vendedor + CIC + monto + ventana)
   =================================== */
MATCHED_LGC_NOSOL AS (
  SELECT
    A.CODMES,
    A.CODSOLICITUD,
    A.DESPRODUCTO,
    A.MATVENDEDOR,
    A.CODINTERNOCOMPUTACIONAL,
    A.MTODESEMBOLSADO AS MTOLINEACREDITO,
    A.FECDESEMBOLSO AS FECEMISIONTARJETA,
    B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
    B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
    B.CODCLAVECTA,
    /* útil para auditoría */
    B.CODSOLICITUD AS CODSOLICITUD_LEGACY_MATCH,
    ROW_NUMBER() OVER (
      PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
      ORDER BY B.FECVENTABCAMINORISTA ASC
    ) AS RN
  FROM PEND_AFTER_LGC A
  JOIN HE_VENTACOMISIONABLEBANCAMINORISTALEGACY B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
   AND A.MATVENDEDOR = B.CODVENDEDORBCAMINORISTALEGACY
   AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
   AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
),
MATCHED_LGC_NOSOL_1 AS (
  SELECT * FROM MATCHED_LGC_NOSOL WHERE RN = 1
),

/* =========================
   UNION final con origen
   ========================= */
BASE_UNION AS (
  SELECT
    CODMES, CODSOLICITUD, DESPRODUCTO,
    MTOLINEACREDITO, FECEMISIONTARJETA, FECVENTATARJETA, MTOVENTATARJETA,
    CODCLAVECTA, CODCLAVETARJETA,
    'MD' AS ORIGEN
  FROM MATCHED_MD_1

  UNION ALL

  SELECT
    CODMES, CODSOLICITUD, DESPRODUCTO,
    MTOLINEACREDITO, FECEMISIONTARJETA, FECVENTATARJETA, MTOVENTATARJETA,
    CODCLAVECTA, CODCLAVETARJETA,
    'MD_SIN_PRODUCTO' AS ORIGEN
  FROM MATCHED_MD_NOPROD_1

  UNION ALL

  SELECT
    CODMES, CODSOLICITUD, DESPRODUCTO,
    MTOLINEACREDITO, FECEMISIONTARJETA, FECVENTATARJETA, MTOVENTATARJETA,
    CODCLAVECTA, CAST(NULL AS STRING) AS CODCLAVETARJETA,
    'LEGACY' AS ORIGEN
  FROM MATCHED_LGC_1

  UNION ALL

  SELECT
    CODMES, CODSOLICITUD, DESPRODUCTO,
    MTOLINEACREDITO, FECEMISIONTARJETA, FECVENTATARJETA, MTOVENTATARJETA,
    CODCLAVECTA, CAST(NULL AS STRING) AS CODCLAVETARJETA,
    'LEGACY_SIN_SOLICITUD' AS ORIGEN
  FROM MATCHED_LGC_NOSOL_1
)

SELECT
    A.CODSOLICITUD,
    A.CODINTERNOCOMPUTACIONAL,
    A.CODPRODUCTO,
    A.MATVENDEDOR,
    A.FECDESEMBOLSO,
    A.MTODESEMBOLSADO
FROM TP_BASE A
LEFT JOIN BASE_UNION B
    ON A.CODSOLICITUD = B.CODSOLICITUD
WHERE B.CODSOLICITUD IS NULL;
