WITH sol AS (
  SELECT
    a.*,
    CAST(regexp_extract(a.NUMSOLICITUD, '(\\d+)', 1) AS BIGINT) AS NUMOP_NORM
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_SOLICITUDCREDITOVEHICULAR a
  WHERE a.NUMSOLICITUD IS NOT NULL
),
veh AS (
  SELECT
    b.*,
    CAST(b.NUMEROOPERACION AS BIGINT) AS NUMOP
  FROM CATALOG_LHCL_PROD_BCP_EXPL.BCP_EDV_RBMBDN.T72496_BASE_VEHICULAR b
  WHERE b.NUMEROOPERACION IS NOT NULL
)
SELECT s.*
FROM sol s
JOIN veh v
  ON s.NUMOP_NORM = v.NUMOP;
