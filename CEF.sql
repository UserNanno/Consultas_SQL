WITH
TP_BASE AS (
  SELECT
      A.CODMES,
      A.CODSOLICITUD,
      A.CODINTERNOCOMPUTACIONAL,
      B.CODCLAVEPARTYCLI,
      UPPER(TRIM(C.CODPRODUCTO)) AS CODPRODUCTO,
      A.FECSOLICITUD,
      A.FECDESEMBOLSO,
      A.MTOSOLICITADO,
      A.MTOAPROBADO,
      A.MTODESEMBOLSADO
  FROM CATALOG_LHCL_PROD_BCP.BCP_EDV_RBMBDN.T72496_HM_SOLICITUDES_SCRM A
  LEFT JOIN CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_CLIENTE B
    ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
  LEFT JOIN CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_PRODUCTOTIOAUX C
    ON UPPER(TRIM(A.DESPRODUCTO)) = UPPER(TRIM(C.DESPRODUCTO))
  WHERE A.CENTROATENCION = 'CENTRALIZADO'
    AND A.DESTIPOPERACION = 'TARJETA CREDITO NUEVA'
    AND A.FLGDESEMBOLSADO = 1
),

TC_CANDIDATAS AS (
  SELECT
      t.CODSOLICITUD,
      t.CODCLAVEPARTYCLI,
      t.CODPRODUCTO,
      t.FECSOLICITUD AS FEC_ANCLA,                      -- <-- cambia a t.FECDESEMBOLSO si ese es tu ancla real
      m.CODCLAVECTA,
      m.FECSOLICITUDTARJETA,
      m.FECINCREMENTOLINEA,
      m.FECULTDIAPAGO,
      COALESCE(m.FECULTCOMPRA, m.FECULTTRX) AS FECULTMOVIMIENTO,

      /* Prioridad: 1 si matchea por FECSOLICITUDTARJETA, 2 si matchea por FECINCREMENTOLINEA */
      CASE
        WHEN m.FECSOLICITUDTARJETA BETWEEN t.FECSOLICITUD AND date_add(t.FECSOLICITUD, 30) THEN 1
        WHEN m.FECINCREMENTOLINEA   BETWEEN t.FECSOLICITUD AND date_add(t.FECSOLICITUD, 30) THEN 2
        ELSE 99
      END AS PRI,

      /* Distancia en días para escoger la “más cercana” */
      LEAST(
        CASE WHEN m.FECSOLICITUDTARJETA IS NOT NULL THEN abs(datediff(m.FECSOLICITUDTARJETA, t.FECSOLICITUD)) ELSE 9999 END,
        CASE WHEN m.FECINCREMENTOLINEA   IS NOT NULL THEN abs(datediff(m.FECINCREMENTOLINEA,   t.FECSOLICITUD)) ELSE 9999 END
      ) AS DIAS_DIST
  FROM TP_BASE t
  LEFT JOIN CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_CUENTATARJETACREDITO m
    ON t.CODCLAVEPARTYCLI = m.CODCLAVEPARTYCLI
   AND t.CODPRODUCTO      = m.CODPRODUCTO
   AND m.FECULTDIAPAGO IS NOT NULL
   AND (
        m.FECSOLICITUDTARJETA BETWEEN t.FECSOLICITUD AND date_add(t.FECSOLICITUD, 30)
     OR m.FECINCREMENTOLINEA   BETWEEN t.FECSOLICITUD AND date_add(t.FECSOLICITUD, 30)
   )
),

TC_ELEGIDA AS (
  SELECT *
  FROM (
    SELECT
        c.*,
        row_number() OVER (
          PARTITION BY c.CODSOLICITUD
          ORDER BY
            c.PRI ASC,
            c.DIAS_DIST ASC,
            c.FECINCREMENTOLINEA DESC,
            c.CODCLAVECTA
        ) AS RN
    FROM TC_CANDIDATAS c
    WHERE c.PRI IN (1, 2)
  ) x
  WHERE x.RN = 1
)

SELECT
    t.*,
    e.CODCLAVECTA,
    e.FECSOLICITUDTARJETA,
    e.FECINCREMENTOLINEA,
    e.FECULTMOVIMIENTO,
    e.PRI AS ORIGEN_MATCH
FROM TP_BASE t
LEFT JOIN TC_ELEGIDA e
  ON t.CODSOLICITUD = e.CODSOLICITUD;
