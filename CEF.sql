WITH
BASE_VEHICULAR AS (
	SELECT
		A.CODMES,
		UPPER(TRIM(A.CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
		COALESCE(A.FLGCAMPANIA, 0) AS FLGLEAD,
		UPPER(TRIM(A.DECISIONANALISTA)) AS DECISIONANALISTA,
		A.MTOFINANCIADO,
		B.CODCLAVEPARTYCLI
	FROM CATALOG_LHCL_PROD_BCP_EXPL.BCP_EDV_RBMBDN.T72496_VEHICULAR A
	LEFT JOIN CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_CLIENTE B ON UPPER(TRIM(A.CODINTERNOCOMPUTACIONAL)) = UPPER(TRIM(B.CODINTERNOCOMPUTACIONAL))
	WHERE A.FLGDESEMBOLSO = 0 AND A.DECISIONANALISTA IN ('APROBADO','RECHAZADO','DEVUELTO') AND A.CODMES BETWEEN 202508 AND 202510
),
CLIENTE_MES AS (
	SELECT
	CODMES,
	CODCLAVEPARTYCLI,
	MAX(COALESCE(MTOFINANCIADO,0)) AS MTOFINANCIADO_MAX,
	MAX(CASE WHEN FLGLEAD = 1 AND DECISIONANALISTA = 'RECHAZADO' THEN 1 ELSE 0 END) AS FLG_LEAD_RECHAZADO,
	MAX(CASE WHEN FLGLEAD = 1 AND DECISIONANALISTA = 'APROBADO' THEN 1 ELSE 0 END) AS FLG_LEAD_APROBADO
  FROM BASE_VEHICULAR
  WHERE CODCLAVEPARTYCLI IS NOT NULL
  GROUP BY CODMES, CODCLAVEPARTYCLI
),

SBS AS (
	SELECT
	CAST(date_format(FECDIA,'yyyyMM') AS INT) AS CODMES,
	CODCLAVEPARTYCLI,
	NBREMPSISTEMAFINANCIERO,
	DESTIPCREDITO,
	DESCTACONTABLESBS,
	DESTIPCLASIFRIESGO,
	CTDDIAMOROSIDAD,
	MTODEUDASOL
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_DEUDORSBSDETALLE
	WHERE UPPER(TRIM(NBREMPSISTEMAFINANCIERO)) NOT LIKE 'BANCO DE CREDITO DEL PER%'
	AND UPPER(TRIM(DESCTACONTABLESBS)) IN (
      'CRÉDITOS NO REVOLVENTES',
      'PRÉSTAMOS ASOCIADOS A LÍNEAS REVOLVENTES',
      'OTROS CRÉDITOS DE CONSUMO',
      'DISPOSICIÓN DE EFECTIVO',
      'PRÉSTAMOS NO REVOLVENTES PARA AUTOMÓVILES',
      'PRÉSTAMOS NO REVOLVENTES PARA LIBRE DISPONIBILIDAD'
    )
),

SBS_ACTUAL AS (
  SELECT *
  FROM SBS
),

SBS_ANTERIOR AS (
	SELECT
	CODCLAVEPARTYCLI,
	CAST(date_format(add_months(to_date(concat(CAST(CODMES AS STRING),'01'),'yyyyMMdd'),1),'yyyyMM') AS INT) AS CODMES,
	NBREMPSISTEMAFINANCIERO,
	MTODEUDASOL
	FROM SBS
),

LINEA_NUEVA AS (
  SELECT
    a.*
  FROM SBS_ACTUAL a
  LEFT JOIN SBS_ANTERIOR b
    ON a.CODCLAVEPARTYCLI = b.CODCLAVEPARTYCLI
   AND a.CODMES = b.CODMES
   AND a.NBREMPSISTEMAFINANCIERO = b.NBREMPSISTEMAFINANCIERO
  WHERE b.CODCLAVEPARTYCLI IS NULL
),

CLIENTE_FUGA AS (
  SELECT
    c.CODMES,
    c.CODCLAVEPARTYCLI,
    c.MTOFINANCIADO_MAX,

    s.NBREMPSISTEMAFINANCIERO,
    s.DESTIPCREDITO,
    s.DESCTACONTABLESBS,
    s.DESTIPCLASIFRIESGO,
    s.CTDDIAMOROSIDAD,
    s.MTODEUDASOL,

    0.5 * c.MTOFINANCIADO_MAX AS UMBRAL_50

  FROM CLIENTE_MES c
  INNER JOIN LINEA_NUEVA s
    ON c.CODCLAVEPARTYCLI = s.CODCLAVEPARTYCLI
   AND c.CODMES = s.CODMES
  WHERE c.MTOFINANCIADO_MAX > 0
    AND s.MTODEUDASOL >= 0.5 * c.MTOFINANCIADO_MAX
)

SELECT *
FROM CLIENTE_FUGA
ORDER BY CODMES, CODCLAVEPARTYCLI;
