La query que arme es algo asi:

WITH
HM_SOLICITUDES_SCRM AS (
	SELECT
		CODMES,
		CODSOLICITUD,
		CODINTERNOCOMPUTACIONAL,
		UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO,
		FECSOLICITUD,
		MTOSOLICITADO,
		MTOAPROBADO,
		FECDESEMBOLSO,
		MTODESEMBOLSADO
	FROM CATALOG_LHCL_PROD_BCP.BCP_EDV_RBMBDN.T72496_HM_SOLICITUDES_SCRM
	WHERE CENTROATENCION = 'CENTRALIZADO' AND DESTIPOPERACION = 'TARJETA CREDITO NUEVA' AND FLGDESEMBOLSADO = 1
),
M_CLIENTE AS (
	SELECT
		CODCLAVEPARTYCLI,
		CODINTERNOCOMPUTACIONAL
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_CLIENTE
),
M_PRODUCTOTIOAUX AS (
	SELECT
		CODPRODUCTO,
		UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_PRODUCTOTIOAUX
),
M_CUENTATARJETACREDITO AS (
	SELECT
		CODCLAVECTA,
		CODCLAVEPARTYCLI,
		CODPRODUCTO,
		FECSOLICITUDTARJETA,
		FECINCREMENTOLINEA,
		COALESCE(FECULTCOMPRA, FECULTTRX) AS FECULTMOVIMIENTO,
		ROW_NUMBER() OVER (
			PARTITION BY CODCLAVECTA
			ORDER BY FECINCREMENTOLINEA ASC
		) AS RN
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_CUENTATARJETACREDITO
	WHERE FECSOLICITUDTARJETA >= DATE(FECINCREMENTOLINEA) AND FECULTDIAPAGO IS NOT NULL
),
M_CUENTATARJETACREDITO_UNICO AS (
	SELECT
		CODCLAVECTA,
		CODCLAVEPARTYCLI,
		CODPRODUCTO,
		FECSOLICITUDTARJETA,
		FECULTMOVIMIENTO,
		FECINCREMENTOLINEA
	FROM M_CUENTATARJETACREDITO
	WHERE RN = 1
),
M_CUENTATARJETACREDITO_FB AS (
	SELECT
		CODCLAVECTA,
		CODCLAVEPARTYCLI,
		CODPRODUCTO,
		FECSOLICITUDTARJETA,
		FECINCREMENTOLINEA,
		COALESCE(FECULTCOMPRA, FECULTTRX) AS FECULTMOVIMIENTO,
		ROW_NUMBER() OVER (
			PARTITION BY CODCLAVEPARTYCLI
			ORDER BY FECINCREMENTOLINEA DESC
		) AS RN
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_CUENTATARJETACREDITO
	WHERE FECULTDIAPAGO IS NOT NULL
),
M_CUENTATARJETACREDITO_UNICO_FB AS (
	SELECT
		CODCLAVECTA,
		CODCLAVEPARTYCLI,
		CODPRODUCTO,
		FECSOLICITUDTARJETA,
		FECULTMOVIMIENTO,
		FECINCREMENTOLINEA
	FROM M_CUENTATARJETACREDITO
	WHERE RN = 1
),
TP_BASE_1 AS (
	SELECT
		A.CODSOLICITUD,
		A.FECSOLICITUD,
		A.CODINTERNOCOMPUTACIONAL,
		B.CODCLAVEPARTYCLI,
		C.CODPRODUCTO,
		A.MTOSOLICITADO,
		A.MTOAPROBADO,
		A.MTODESEMBOLSADO,
		A.FECDESEMBOLSO
	FROM HM_SOLICITUDES_SCRM A
	LEFT JOIN M_CLIENTE B ON (A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL)
	LEFT JOIN M_PRODUCTOTIOAUX C ON (A.DESPRODUCTO = C.DESPRODUCTO)
)
	SELECT
		A.CODSOLICITUD,
		A.FECSOLICITUD,
		A.CODINTERNOCOMPUTACIONAL,
		A.MTOSOLICITADO,
		A.MTODESEMBOLSADO,
		A.FECDESEMBOLSO,
		COALESCE(B.FECINCREMENTOLINEA, C.FECINCREMENTOLINEA) AS FECINCREMENTOLINEA,
		COALESCE(B.FECULTMOVIMIENTO, C.FECULTMOVIMIENTO) AS FECULTMOVIMIENTO,
		COALESCE(B.CODCLAVECTA, C.CODCLAVECTA) AS CODCLAVECTA
	FROM TP_BASE_1 A
	LEFT JOIN M_CUENTATARJETACREDITO_UNICO B ON (A.CODCLAVEPARTYCLI = B.CODCLAVEPARTYCLI AND A.CODPRODUCTO = B.CODPRODUCTO AND B.FECINCREMENTOLINEA >= A.FECDESEMBOLSO AND B.FECINCREMENTOLINEA <= A.FECDESEMBOLSO + INTERVAL 30 DAYS)
	LEFT JOIN M_CUENTATARJETACREDITO_UNICO_FB C ON (A.CODCLAVEPARTYCLI = C.CODCLAVEPARTYCLI AND A.CODPRODUCTO = C.CODPRODUCTO AND C.FECSOLICITUDTARJETA = A.FECDESEMBOLSO)
	

