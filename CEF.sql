WITH
HM_SOLICITUDES_SCRM AS (
	SELECT
		CODMES,
		CODSOLICITUD,
		UPPER(TRIM(CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
		UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO,
		FECSOLICITUD,
		FECDESEMBOLSO,
		MTODESEMBOLSADO
	FROM CATALOG_LHCL_PROD_BCP.BCP_EDV_RBMBDN.T72496_HM_SOLICITUDES_SCRM
	WHERE CENTROATENCION = 'CENTRALIZADO' AND DESTIPOPERACION = 'TARJETA CREDITO NUEVA' AND FLGDESEMBOLSADO = 1
),
M_PRODUCTOTIOAUX AS (
	SELECT
		CODPRODUCTO,
		UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_PRODUCTOTIOAUX
),
TP_BASE AS (
	SELECT
		A.CODMES,
		A.CODSOLICITUD,
		A.FECSOLICITUD,
		A.CODINTERNOCOMPUTACIONAL,
		A.DESPRODUCTO,
		B.CODPRODUCTO,
		A.FECDESEMBOLSO,
		A.MTODESEMBOLSADO
	FROM HM_SOLICITUDES_SCRM A
	LEFT JOIN M_PRODUCTOTIOAUX B ON (A.DESPRODUCTO = B.DESPRODUCTO)
),
MD_VENTACOMISIONABLEBANCAMINORISTA AS (
	SELECT
		DISTINCT
		CODSOLICITUD,
		UPPER(TRIM(CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
		FECVENTABCAMINORISTA,
		MTOVENTABCAMINORISTA,
		CODPRODUCTO,
		CODCLAVECTA,
		CODCLAVETARJETA
	FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.MD_VENTACOMISIONABLEBANCAMINORISTA
	WHERE DESAPPVENTABCAMINORISTA = 'COE SALESFORCE' AND DESTIPPRODUCTOBCAMINORISTA = 'TARJETA DE CREDITO'
	AND UPPER(TRIM(DESPRODUCTOCOMERCIALBCAMINORISTA)) LIKE '%VENTA%'
)
SELECT
	A.CODMES,
	A.CODSOLICITUD,
	A.DESPRODUCTO,
	A.MTODESEMBOLSADO AS MTOLINEACREDITO,
	A.FECDESEMBOLSO AS FECEMISIONTARJETA,
	B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
	B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
	B.CODCLAVECTA,
	B.CODCLAVETARJETA
FROM TP_BASE A
JOIN MD_VENTACOMISIONABLEBANCAMINORISTA B 
ON (
	A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL 
	AND A.CODPRODUCTO = B.CODPRODUCTO 
	AND A.CODSOLICITUD = B.CODSOLICITUD 
	AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA 
	AND (
		B.FECVENTABCAMINORISTA = A.FECDESEMBOLSO OR B.FECVENTABCAMINORISTA >= A.FECDESEMBOLSO + INTERVAL 2 DAYS
	)
)
