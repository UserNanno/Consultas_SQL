SELECT A.CODSOLICITUD, A.FECSOLICITUD, B.NBRAREAUNIDADORGANIZATIVA, B.NBRSERVUNIDADORGANIZATIVA
FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_SOLICITUDTARJETACREDITO A
LEFT JOIN CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.H_COLABORADOR B ON (A.FECSOLICITUD = B.FECDIA AND A.CODMATRICULACOLABORADORVENDEDOR = B.CODMATRICULA)
WHERE A.CODMATRICULACOLABORADORVENDEDOR = 'E15812' AND date_format(A.FECSOLICITUD, 'yyyyMM') = 202510;
