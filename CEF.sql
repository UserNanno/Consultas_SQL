  FROM (
    SELECT
      A.CODMES,
      A.CODSOLICITUD,
      A.DESPRODUCTO,
      A.MTODESEMBOLSADO AS MTOLINEACREDITO,
      A.FECDESEMBOLSO AS FECEMISIONTARJETA,
      B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
      B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
      B.CODCLAVECTA,
      B.CODCLAVETARJETA,
      'MD_1C_SIN_SOLICITUD' AS ORIGEN,
      ROW_NUMBER() OVER (
        PARTITION BY A.CODSOLICITUD
        ORDER BY B.FECVENTABCAMINORISTA ASC
      ) AS rn_pick
    FROM PEND_MD_1C A
    JOIN MD_VENTACOMISIONABLEBANCAMINORISTA B
      ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
     AND A.CODPRODUCTO = B.CODPRODUCTO
     AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
     AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
  ) t
  WHERE rn_pick = 1
),

PEND_MD_1D AS (
  SELECT A.*
  FROM PEND_MD_1C A
  LEFT ANTI JOIN MATCH_MD_1C M
    ON A.CODSOLICITUD = M.CODSOLICITUD
),

-- 1D: sin monto con tolerancia (COE)
MATCH_MD_1D AS (
  SELECT
    CODMES,CODSOLICITUD,DESPRODUCTO,
    MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,
    CODCLAVECTA,CODCLAVETARJETA,ORIGEN
  FROM (
    SELECT
      A.CODMES,
      A.CODSOLICITUD,
      A.DESPRODUCTO,
      A.MTODESEMBOLSADO AS MTOLINEACREDITO,
      A.FECDESEMBOLSO AS FECEMISIONTARJETA,
      B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
      B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
      B.CODCLAVECTA,
      B.CODCLAVETARJETA,
      'MD_1D_SIN_MONTO' AS ORIGEN,
      ROW_NUMBER() OVER (
        PARTITION BY A.CODSOLICITUD
        ORDER BY ABS(A.MTODESEMBOLSADO - B.MTOVENTABCAMINORISTA) ASC,
                 B.FECVENTABCAMINORISTA ASC
      ) AS rn_pick
    FROM PEND_MD_1D A
    JOIN MD_VENTACOMISIONABLEBANCAMINORISTA B
      ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
     AND A.CODPRODUCTO = B.CODPRODUCTO
     AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
     AND ABS(A.MTODESEMBOLSADO - B.MTOVENTABCAMINORISTA) <= 1
  ) t
  WHERE rn_pick = 1
),

PEND_MD_1E AS (
  SELECT A.*
  FROM PEND_MD_1D A
  LEFT ANTI JOIN MATCH_MD_1D M
    ON A.CODSOLICITUD = M.CODSOLICITUD
),

-- 1E: ALL APPS (sin cods vend app)
MATCH_MD_1E AS (
  SELECT
    CODMES,CODSOLICITUD,DESPRODUCTO,
    MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,
    CODCLAVECTA,CODCLAVETARJETA,ORIGEN
  FROM (
    SELECT
      A.CODMES,
      A.CODSOLICITUD,
      A.DESPRODUCTO,
      A.MTODESEMBOLSADO AS MTOLINEACREDITO,
      A.FECDESEMBOLSO AS FECEMISIONTARJETA,
      B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
      B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
      B.CODCLAVECTA,
      B.CODCLAVETARJETA,
      'MD_1E_SIN_CODS_VEND_APP' AS ORIGEN,
      ROW_NUMBER() OVER (
        PARTITION BY A.CODSOLICITUD
        ORDER BY ABS(A.MTODESEMBOLSADO - B.MTOVENTABCAMINORISTA) ASC,
                 B.FECVENTABCAMINORISTA ASC
      ) AS rn_pick
    FROM PEND_MD_1E A
    JOIN MD_VENTACOMISIONABLEBANCAMINORISTA_NOAPP B
      ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
     AND A.CODPRODUCTO = B.CODPRODUCTO
     AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
  ) t
  WHERE rn_pick = 1
),

PEND_MD_1F AS (
  SELECT A.*
  FROM PEND_MD_1E A
  LEFT ANTI JOIN MATCH_MD_1E M
    ON A.CODSOLICITUD = M.CODSOLICITUD
),

-- 1F: ALL APPS, sin CODSOLICITUD (pero aquí tu join sí usa A.CODPRODUCTO + monto + ventana)
MATCH_MD_1F AS (
  SELECT
    CODMES,CODSOLICITUD,DESPRODUCTO,
    MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,
    CODCLAVECTA,CODCLAVETARJETA,ORIGEN
  FROM (
    SELECT
      A.CODMES,
      A.CODSOLICITUD,
      A.DESPRODUCTO,
      A.MTODESEMBOLSADO AS MTOLINEACREDITO,
      A.FECDESEMBOLSO AS FECEMISIONTARJETA,
      B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
      B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
