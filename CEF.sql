-- =========================================================
-- OBJETIVO:
--   1) Un solo CTAS (1 write Delta) -> mejor performance
--   2) Patrón cascada + pendientes (gating) con LEFT ANTI JOIN
--   3) Nomenclatura:
--        - Fuentes con nombre de tabla (CTE)
--        - Base: TP_BASE
--        - Cascada: MATCH_MD_1A / PEND_MD_1B / ...
--   4) DISTINCT donde ayuda (fuentes MD/LGC y MD_OK)
-- =========================================================

DROP TABLE IF EXISTS CATALOG_LHCL_BCP_EXPL.BCP_EDV_RBMBDN.T72496_TP_FINAL_BASE;

CREATE TABLE CATALOG_LHCL_BCP_EXPL.BCP_EDV_RBMBDN.T72496_TP_FINAL_BASE
USING DELTA
AS
WITH
-- =========================================================
-- 1) FUENTES (con nombre “completo”)
-- =========================================================
HM_SOLICITUDES_SCRM AS (
  SELECT
    CODMES,
    TRIM(CODSOLICITUD) AS CODSOLICITUD,
    CASE
      WHEN UPPER(TRIM(CODINTERNOCOMPUTACIONAL)) LIKE '%-X%' THEN
        CONCAT('    ', REGEXP_REPLACE(UPPER(TRIM(CODINTERNOCOMPUTACIONAL)), '-X', ''))
      ELSE
        UPPER(TRIM(CODINTERNOCOMPUTACIONAL))
    END AS CODINTERNOCOMPUTACIONAL,
    UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO,
    TRIM(MATVENDEDOR) AS MATVENDEDOR,
    FECSOLICITUD,
    FECDESEMBOLSO,
    MTODESEMBOLSADO
  FROM CATALOG_LHCL_PROD_BCP.BCP_EDV_RBMBDN.T72496_HM_SOLICITUDES_SCRM
  WHERE CENTROATENCION = 'CENTRALIZADO'
    AND DESTIPOPERACION = 'TARJETA CREDITO NUEVA'
    AND FLGDESEMBOLSADO = 1
),

M_PRODUCTOTIOAUX AS (
  SELECT
    CODPRODUCTO,
    UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_PRODUCTOTIOAUX
),

-- MD (COE)
MD_VENTACOMISIONABLEBANCAMINORISTA AS (
  SELECT DISTINCT
    TRIM(CODSOLICITUD) AS CODSOLICITUD,
    UPPER(TRIM(CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    CODPRODUCTO,
    CODCLAVECTA,
    CODCLAVETARJETA,
    TRIM(CODVENDEDORCOMERCIALBCAMINORISTA) AS CODVENDEDORCOMERCIALBCAMINORISTA
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.MD_VENTACOMISIONABLEBANCAMINORISTA
  WHERE DESAPPVENTABCAMINORISTA = 'COE SALESFORCE'
    AND DESTIPPRODUCTOBCAMINORISTA = 'TARJETA DE CREDITO'
    AND UPPER(TRIM(DESPRODUCTOCOMERCIALBCAMINORISTA)) LIKE '%VENTA%'
),

-- MD (ALL APPS)
MD_VENTACOMISIONABLEBANCAMINORISTA_NOAPP AS (
  SELECT DISTINCT
    TRIM(CODSOLICITUD) AS CODSOLICITUD,
    UPPER(TRIM(CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    CODPRODUCTO,
    CODCLAVECTA,
    CODCLAVETARJETA,
    TRIM(CODVENDEDORCOMERCIALBCAMINORISTA) AS CODVENDEDORCOMERCIALBCAMINORISTA
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.MD_VENTACOMISIONABLEBANCAMINORISTA
  WHERE DESTIPPRODUCTOBCAMINORISTA = 'TARJETA DE CREDITO'
    AND UPPER(TRIM(DESPRODUCTOCOMERCIALBCAMINORISTA)) LIKE '%VENTA%'
),

-- LEGACY (filtrado por producto)
HE_VENTACOMISIONABLEBANCAMINORISTALEGACY AS (
  SELECT DISTINCT
    TRIM(CODUNICOVENTABCAMINORISTALEGACY) AS CODSOLICITUD,
    UPPER(TRIM(CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    TRIM(CODVENDEDORBCAMINORISTALEGACY) AS CODVENDEDORBCAMINORISTALEGACY,
    CODCLAVECTA
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.HE_VENTACOMISIONABLEBANCAMINORISTALEGACY
  WHERE CODPRODUCTOCOMERCIALBCAMINORISTALEGACY IN ('17', '18')
),

-- LEGACY (todos los prod)
HE_VENTACOMISIONABLEBANCAMINORISTALEGACY_NOCODPROD AS (
  SELECT DISTINCT
    TRIM(CODUNICOVENTABCAMINORISTALEGACY) AS CODSOLICITUD,
    UPPER(TRIM(CODINTERNOCOMPUTACIONAL)) AS CODINTERNOCOMPUTACIONAL,
    FECVENTABCAMINORISTA,
    MTOVENTABCAMINORISTA,
    TRIM(CODVENDEDORBCAMINORISTALEGACY) AS CODVENDEDORBCAMINORISTALEGACY,
    CODCLAVECTA
  FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.HE_VENTACOMISIONABLEBANCAMINORISTALEGACY
),

-- =========================================================
-- 2) BASE
-- =========================================================
TP_BASE AS (
  SELECT
    A.CODMES,
    A.CODSOLICITUD,
    A.FECSOLICITUD,
    A.CODINTERNOCOMPUTACIONAL,
    A.DESPRODUCTO,
    A.MATVENDEDOR,
    B.CODPRODUCTO,
    A.FECDESEMBOLSO,
    A.MTODESEMBOLSADO
  FROM HM_SOLICITUDES_SCRM A
  LEFT JOIN M_PRODUCTOTIOAUX B
    ON A.DESPRODUCTO = B.DESPRODUCTO
),

-- =========================================================
-- 3) CASCADA MD (gating)
-- =========================================================

-- 1A: estricto (solicitud+producto+monto+ventana, COE)
MATCH_MD_1A AS (
  SELECT
    CODMES,CODSOLICITUD,DESPRODUCTO,
    MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,
    CODCLAVECTA,CODCLAVETARJETA,ORIGEN
  FROM (
    SELECT
      A.CODMES,
      A.CODSOLICITUD,
      A.DESPRODUCTO,
      A.MTODESEMBOLSADO AS MTOLINEACREDITO,
      A.FECDESEMBOLSO AS FECEMISIONTARJETA,
      B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
      B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
      B.CODCLAVECTA,
      B.CODCLAVETARJETA,
      'MD_1A' AS ORIGEN,
      ROW_NUMBER() OVER (
        PARTITION BY A.CODSOLICITUD
        ORDER BY B.FECVENTABCAMINORISTA ASC
      ) AS rn_pick
    FROM TP_BASE A
    JOIN MD_VENTACOMISIONABLEBANCAMINORISTA B
      ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
     AND A.CODSOLICITUD = B.CODSOLICITUD
     AND A.CODPRODUCTO = B.CODPRODUCTO
     AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
     AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
  ) t
  WHERE rn_pick = 1
),

PEND_MD_1B AS (
  SELECT A.*
  FROM TP_BASE A
  LEFT ANTI JOIN MATCH_MD_1A M
    ON A.CODSOLICITUD = M.CODSOLICITUD
),

-- 1B: sin producto (solicitud+monto+ventana, COE)
MATCH_MD_1B AS (
  SELECT
    CODMES,CODSOLICITUD,DESPRODUCTO,
    MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,
    CODCLAVECTA,CODCLAVETARJETA,ORIGEN
  FROM (
    SELECT
      A.CODMES,
      A.CODSOLICITUD,
      A.DESPRODUCTO,
      A.MTODESEMBOLSADO AS MTOLINEACREDITO,
      A.FECDESEMBOLSO AS FECEMISIONTARJETA,
      B.FECVENTABCAMINORISTA AS FECVENTATARJETA,
      B.MTOVENTABCAMINORISTA AS MTOVENTATARJETA,
      B.CODCLAVECTA,
      B.CODCLAVETARJETA,
      'MD_1B_SIN_PRODUCTO' AS ORIGEN,
      ROW_NUMBER() OVER (
        PARTITION BY A.CODSOLICITUD
        ORDER BY B.FECVENTABCAMINORISTA ASC
      ) AS rn_pick
    FROM PEND_MD_1B A
    JOIN MD_VENTACOMISIONABLEBANCAMINORISTA B
      ON A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
     AND A.CODSOLICITUD = B.CODSOLICITUD
     AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
     AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
  ) t
  WHERE rn_pick = 1
),

PEND_MD_1C AS (
  SELECT A.*
  FROM PEND_MD_1B A
  LEFT ANTI JOIN MATCH_MD_1B M
    ON A.CODSOLICITUD = M.CODSOLICITUD
),

-- 1C: sin CODSOLICITUD (producto+monto+ventana, COE)
MATCH_MD_1C AS (
  SELECT
    CODMES,CODSOLICITUD,DESPRODUCTO,
    MTOLINEACREDITO,FECEMISIONTARJETA,FECVENTATARJETA,MTOVENTATARJETA,
