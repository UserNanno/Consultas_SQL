WITH
M_SOLICITUDCREDITOVEHICULAR AS (
	SELECT
		CAST(date_format(FECCREACIONSOLICITUD, 'yyyyMM') AS INT) AS CODMES,
		CAST(regexp_extract(NUMSOLICITUD, '(\\d+)', 1) AS BIGINT) AS CODSOLICITUD_NORM,
		NUMSOLICITUD AS CODSOLICITUD,
		CODMATRICULAVENDEDOR,
		DESTIPESTADOSOLICITUD,
		FECCREACIONSOLICITUD,
		FECDESEMBOLSO,
		CODINTERNOCOMPUTACIONALCLI AS CODINTERNOCOMPUTACIONAL
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_SOLICITUDCREDITOVEHICULAR
	WHERE CODAPP = 'SMAR'
),
BASE_VEHICULAR AS (
	SELECT
		CODMESCREACION AS CODMES,
		NUMEROOPERACION AS CODSOLICITUD
	FROM CATALOG_LHCL_PROD_BCP_EXPL.BCP_EDV_RBMBDN.T72496_BASE_VEHICULAR
)
SELECT
	A.CODMES,
	A.CODSOLICITUD,
	B.DESTIPESTADOSOLICITUD,
	B.FECDESEMBOLSO,
	B.CODINTERNOCOMPUTACIONAL
FROM BASE_VEHICULAR A
JOIN M_SOLICITUDCREDITOVEHICULAR B ON (A.CODSOLICITUD = B.CODSOLICITUD_NORM);
