WITH
HM_SOLICITUDES_SCRM AS (
	SELECT
		CODMES,
		CODSOLICITUD,
		(CASE
			WHEN UPPER(TRIM(CODINTERNOCOMPUTACIONAL)) LIKE '%-X%' THEN CONCAT('    ', REGEXP_REPLACE(UPPER(TRIM(CODINTERNOCOMPUTACIONAL)), '-X', '')))
			ELSE CODINTERNOCOMPUTACIONAL
		END) AS CODINTERNOCOMPUTACIONAL,
		UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO,
		MATVENDEDOR,
		FECSOLICITUD,
		FECDESEMBOLSO,
		MTODESEMBOLSADO
	FROM CATALOG_LHCL_PROD_BCP.BCP_EDV_RBMBDN.T72496_HM_SOLICITUDES_SCRM
	WHERE CENTROATENCION = 'CENTRALIZADO' AND DESTIPOPERACION = 'TARJETA CREDITO NUEVA' AND FLGDESEMBOLSADO = 1
),
M_PRODUCTOTIOAUX AS (
	SELECT
		CODPRODUCTO,
		UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO
	FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_PRODUCTOTIOAUX
),
TP_BASE AS (
	SELECT
		A.CODMES,
		A.CODSOLICITUD,
		A.FECSOLICITUD,
		A.CODINTERNOCOMPUTACIONAL,
		A.DESPRODUCTO,
		A.MATVENDEDOR,
		B.CODPRODUCTO,
		A.FECDESEMBOLSO,
		A.MTODESEMBOLSADO
	FROM HM_SOLICITUDES_SCRM A
	LEFT JOIN M_PRODUCTOTIOAUX B ON (A.DESPRODUCTO = B.DESPRODUCTO)
),
MD_VENTACOMISIONABLEBANCAMINORISTA_1 AS (
	SELECT
		CODSOLICITUD,
		CODINTERNOCOMPUTACIONAL,
		FECVENTABCAMINORISTA,
		MTOVENTABCAMINORISTA,
		CODPRODUCTO,
		CODCLAVECTA,
		CODCLAVETARJETA,
		CODVENDEDORCOMERCIALBCAMINORISTA
	FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.MD_VENTACOMISIONABLEBANCAMINORISTA
	WHERE DESAPPVENTABCAMINORISTA = 'COE SALESFORCE' AND DESTIPPRODUCTOBCAMINORISTA = 'TARJETA DE CREDITO' AND UPPER(TRIM(DESPRODUCTOCOMERCIALBCAMINORISTA)) LIKE '%VENTA%'
),
MD_VENTACOMISIONABLEBANCAMINORISTA_2 AS (
	SELECT
		CODSOLICITUD,
		CODINTERNOCOMPUTACIONAL,
		FECVENTABCAMINORISTA,
		MTOVENTABCAMINORISTA,
		CODPRODUCTO,
		CODCLAVECTA,
		CODCLAVETARJETA,
		CODVENDEDORCOMERCIALBCAMINORISTA
	FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.MD_VENTACOMISIONABLEBANCAMINORISTA
	WHERE DESTIPPRODUCTOBCAMINORISTA = 'TARJETA DE CREDITO' AND UPPER(TRIM(DESPRODUCTOCOMERCIALBCAMINORISTA)) LIKE '%VENTA%'
),
HE_VENTACOMISIONABLEBANCAMINORISTALEGACY_1 AS (
	SELECT
		TRIM(CODUNICOVENTABCAMINORISTALEGACY) AS CODSOLICITUD,
		CODINTERNOCOMPUTACIONAL,
		FECVENTABCAMINORISTA,
		MTOVENTABCAMINORISTA,
		CODVENDEDORBCAMINORISTALEGACY,
		CODCLAVECTA
	FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.HE_VENTACOMISIONABLEBANCAMINORISTALEGACY
	WHERE CODPRODUCTOCOMERCIALBCAMINORISTALEGACY IN ('17', '18')
),
HE_VENTACOMISIONABLEBANCAMINORISTALEGACY_2 AS (
	SELECT
		TRIM(CODUNICOVENTABCAMINORISTALEGACY) AS CODSOLICITUD,
		CODINTERNOCOMPUTACIONAL,
		FECVENTABCAMINORISTA,
		MTOVENTABCAMINORISTA,
		CODVENDEDORBCAMINORISTALEGACY,
		CODCLAVECTA
	FROM CATALOG_LHCL_PROD_BCP.BCP_DDV_PBM_REMVAR_VU.HE_VENTACOMISIONABLEBANCAMINORISTALEGACY
),
/* ====================== CASCADA MD ====================== */

/* COLA 1A - ESTRICTO */

MD_1A AS (
	SELECT
		A.CODMES,
		A.CODSOLICITUD,
		A.DESPRODUCTO,
		A.MTODESEMBOLSADO,
		A.FECDESEMBOLSO,
		B.FECVENTABCAMINORISTA,
		B.MTOVENTABCAMINORISTA,
		B.CODCLAVECTA,
		B.CODCLAVETARJETA,
		'MD_1A' AS ORIGEN,
		ROW_NUMBER() OVER (
			PARTITION BY A.CODSOLICITUD, A.CODPRODUCTO, A.MTODESEMBOLSADO
			ORDER BY B.FECVENTABCAMINORISTA ASC
		) AS RN
	FROM TP_BASE
	JOIN MD_VENTACOMISIONABLEBANCAMINORISTA_1 ON (
		A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
		AND A.CODSOLICITUD = B.CODSOLICITUD
		AND A.CODPRODUCTO = B.CODPRODUCTO
		AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
		AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
	)
),
MD_1A_1 AS (SELECT * FROM MD_1A WHERE RN = 1),

/* Cola 1B - SIN PRODUCTO */

PENDIENTE_MD_1B AS (
	SELECT
		A.*
	FROM TP_BASE A
	LEFT JOIN MD_1A_1 B ON (A.CODSOLICITUD = B.CODSOLICITUD)
	WHERE B.CODSOLICITUD IS NULL
),
MD_1B AS (
	SELECT
		A.CODMES,
		A.CODSOLICITUD,
		A.DESPRODUCTO,
		A.MTODESEMBOLSADO,
		A.FECDESEMBOLSO,
		B.FECVENTABCAMINORISTA,
		B.MTOVENTABCAMINORISTA,
		B.CODCLAVECTA,
		B.CODCLAVETARJETA,
		'MD_1B_SIN_PRODUCTO' AS ORIGEN,
		ROW_NUMBER() OVER (
			PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
			ORDER BY B.FECVENTABCAMINORISTA ASC
		) AS RN
	FROM PENDIENTE_MD_1B
	JOIN MD_VENTACOMISIONABLEBANCAMINORISTA_1
	ON (
		A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
		AND A.CODSOLICITUD = B.CODSOLICITUD
		AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
		AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
	)
),
MD_1B_1 AS (SELECT * FROM MD_1B WHERE RN = 1),

/* Cola 1C - SIN CODSOLICITUD */

PENDIENTE_MD_1C AS (
	SELECT
		A.*
	FROM PENDIENTE_MD_1B A
	LEFT JOIN MD_1B_1 B ON (A.CODSOLICITUD = B.CODSOLICITUD)
	WHERE B.CODSOLICITUD IS NULL
),
MD_1C AS (
	SELECT
		A.CODMES,
		A.CODSOLICITUD,
		A.DESPRODUCTO,
		A.MTODESEMBOLSADO,
		A.FECDESEMBOLSO,
		B.FECVENTABCAMINORISTA,
		B.MTOVENTABCAMINORISTA,
		B.CODCLAVECTA,
		B.CODCLAVETARJETA,
		'MD_1C_SIN_SOLICITUD' AS ORIGEN,
		ROW_NUMBER() OVER (
			PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
			ORDER BY B.FECVENTABCAMINORISTA ASC
		) AS RN
	FROM PENDIENTE_MD_1C
	JOIN MD_VENTACOMISIONABLEBANCAMINORISTA_1
	ON (
		A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
		AND A.CODPRODUCTO = B.CODPRODUCTO
		AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
		AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
	)
),
MD_1C_1 AS (SELECT * FROM MD_1C WHERE RN = 1),

/* Cola 1D */

PENDIENTE_MD_1D AS (
	SELECT
		A.*
	FROM PENDIENTE_MD_1C A
	LEFT JOIN MD_1C_1 B ON (A.CODSOLICITUD = B.CODSOLICITUD)
	WHERE B.CODSOLICITUD IS NULL
),
MD_1D AS (
	SELECT
		A.CODMES,
		A.CODSOLICITUD,
		A.DESPRODUCTO,
		A.MTODESEMBOLSADO,
		A.FECDESEMBOLSO,
		B.FECVENTABCAMINORISTA,
		B.MTOVENTABCAMINORISTA,
		B.CODCLAVECTA,
		B.CODCLAVETARJETA,
		'MD_1D_SIN_MONTO' AS ORIGEN,
		ROW_NUMBER() OVER (
			PARTITION BY A.CODSOLICITUD, A.MTODESEMBOLSADO
			ORDER BY B.FECVENTABCAMINORISTA ASC
		) AS RN
	FROM PENDIENTE_MD_1D
	JOIN MD_VENTACOMISIONABLEBANCAMINORISTA_1
	ON (
		A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
		AND A.CODPRODUCTO = B.CODPRODUCTO
		AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
		AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
	)
),
MD_1D_1 AS (SELECT * FROM MD_1D WHERE RN = 1),

/* Cola 1E - SIN DESAPPVENTABCAMINORISTA, SOLICITUD, VENDEDOR */

PENDIENTE_MD_1E AS (
	SELECT
		A.*
	FROM PENDIENTE_MD_1D A
	LEFT JOIN MD_1D_1 B ON (A.CODSOLICITUD = B.CODSOLICITUD)
	WHERE B.CODSOLICITUD IS NULL
),
MD_1E AS (
	SELECT
		A.CODMES,
		A.CODSOLICITUD,
		A.DESPRODUCTO,
		A.MTODESEMBOLSADO,
		A.FECDESEMBOLSO,
		B.FECVENTABCAMINORISTA,
		B.MTOVENTABCAMINORISTA,
		B.CODCLAVECTA,
		B.CODCLAVETARJETA,
		'MD_1E_SIN_CODS_VEND_APP' AS ORIGEN,
		ROW_NUMBER() OVER (
			PARTITION BY A.CODSOLICITUD
			ORDER BY ABS(A.MTODESEMBOLSADO - B.MTOVENTABCAMINORISTA) ASC, B.FECVENTABCAMINORISTA ASC
		) AS RN
	FROM PENDIENTE_MD_1E
	JOIN MD_VENTACOMISIONABLEBANCAMINORISTA_2
	ON (
		A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
		AND A.CODPRODUCTO = B.CODPRODUCTO
		AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
		AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
	)
),
MD_1E_1 AS (SELECT * FROM MD_1E WHERE RN = 1),

/* Cola 1F - SIN CODSOLICITUD*/

PENDIENTE_MD_1E AS (
	SELECT
		A.*
	FROM PENDIENTE_MD_1D A
	LEFT JOIN MD_1D_1 B ON (A.CODSOLICITUD = B.CODSOLICITUD)
	WHERE B.CODSOLICITUD IS NULL
),
MD_1E AS (
	SELECT
		A.CODMES,
		A.CODSOLICITUD,
		A.DESPRODUCTO,
		A.MTODESEMBOLSADO,
		A.FECDESEMBOLSO,
		B.FECVENTABCAMINORISTA,
		B.MTOVENTABCAMINORISTA,
		B.CODCLAVECTA,
		B.CODCLAVETARJETA,
		'MD_1E_SIN_CODS_VEND_APP' AS ORIGEN,
		ROW_NUMBER() OVER (
			PARTITION BY A.CODSOLICITUD
			ORDER BY ABS(A.MTODESEMBOLSADO - B.MTOVENTABCAMINORISTA) ASC, B.FECVENTABCAMINORISTA ASC
		) AS RN
	FROM PENDIENTE_MD_1E
	JOIN MD_VENTACOMISIONABLEBANCAMINORISTA_2
	ON (
		A.CODINTERNOCOMPUTACIONAL = B.CODINTERNOCOMPUTACIONAL
		AND A.CODPRODUCTO = B.CODPRODUCTO
		AND A.MTODESEMBOLSADO = B.MTOVENTABCAMINORISTA
		AND B.FECVENTABCAMINORISTA BETWEEN A.FECDESEMBOLSO AND (A.FECDESEMBOLSO + INTERVAL 2 DAYS)
	)
),
MD_1E_1 AS (SELECT * FROM MD_1E WHERE RN = 1),



