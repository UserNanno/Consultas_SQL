-- 1) TP_BASE
-- =========================================================
CREATE TABLE CATALOG_LHCL_BCP_EXPL.BCP_EDV_RBMBDN.T72496_TP_BASE
USING DELTA
AS
WITH
HM_SOLICITUDES_SCRM AS (
  SELECT
    CODMES,
    CODSOLICITUD,
    (CASE
      WHEN UPPER(TRIM(CODINTERNOCOMPUTACIONAL)) LIKE '%-X%' THEN CONCAT('    ', REGEXP_REPLACE(UPPER(TRIM(CODINTERNOCOMPUTACIONAL)), '-X', ''))
      ELSE UPPER(TRIM(CODINTERNOCOMPUTACIONAL))
    END) AS CODINTERNOCOMPUTACIONAL,
    UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO,
    MATVENDEDOR,
    FECSOLICITUD,
    FECDESEMBOLSO,
    MTODESEMBOLSADO
  FROM CATALOG_LHCL_PROD_BCP.BCP_EDV_RBMBDN.T72496_HM_SOLICITUDES_SCRM
  WHERE CENTROATENCION = 'CENTRALIZADO'
    AND DESTIPOPERACION = 'TARJETA CREDITO NUEVA'
    AND FLGDESEMBOLSADO = 1
),
M_PRODUCTOTIOAUX AS (
  SELECT
    CODPRODUCTO,
    UPPER(TRIM(DESPRODUCTO)) AS DESPRODUCTO
  FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_PRODUCTOTIOAUX
)
SELECT
  A.CODMES,
  A.CODSOLICITUD,
  A.FECSOLICITUD,
  A.CODINTERNOCOMPUTACIONAL,
  A.DESPRODUCTO,
  A.MATVENDEDOR,
  B.CODPRODUCTO,
  A.FECDESEMBOLSO,
  A.MTODESEMBOLSADO
FROM HM_SOLICITUDES_SCRM A
LEFT JOIN M_PRODUCTOTIOAUX B
  ON A.DESPRODUCTO = B.DESPRODUCTO
;

