DIMMES =
VAR Base =
    DISTINCT(
        FILTER(
            UNION(
                SELECTCOLUMNS('WEEKLY', "CODMES", 'WEEKLY'[CODMES]),
                SELECTCOLUMNS('WEEKLY', "CODMES", 'WEEKLY'[CODMESDESEMBOLSO])
            ),
            NOT ISBLANK([CODMES])
        )
    )
RETURN
ADDCOLUMNS(
   Base,
   "FechaMes", DATE(INT([CODMES] / 100), MOD([CODMES], 100), 1),
   "Anio", INT([CODMES] / 100),
   "MesNum", MOD([CODMES], 100),
   "MesNombre", FORMAT(DATE(INT([CODMES] / 100), MOD([CODMES], 100), 1), "MMM YYYY"),
   "MesIndex", INT([CODMES] / 100) * 12 + MOD([CODMES], 100)
)



CTDSOLICITUDESDESEMBOLSADAS_MESDESEMBOLSO =
CALCULATE(
    SUM(WEEKLY[CTDSOLICITUDESDESEMBOLSADAS]),
    USERELATIONSHIP(DIMMES[CODMES], WEEKLY[CODMESDESEMBOLSO])
)
