COALESCE(
  /* BUSCARV(...; AW:AX; 2; 0) */
  CASE
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'DENEGADO'                         THEN 'DENEGADAS'
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'APROBADO'                         THEN 'ACEPTADAS'
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'FACA'                             THEN 'FACA'
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'DESESTIMADO'                      THEN 'DESACTIVADA'
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'ACEPTADA'                         THEN 'ACEPTADAS'
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'DENEGADO POR ANALISTA DE CREDITO' THEN 'DENEGADAS'
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'APROBADO POR ANALISTA DE CREDITO' THEN 'ACEPTADAS'
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'EN PROCESO'                       THEN 'EN PROCESO'
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'SOLICITUD CANCELADA'              THEN 'SOLICITUD CANCELADA'
    WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'ENVIADO AL CENTRALIZADO'          THEN 'ENVIADO AL CENTRALIZADO'
    ELSE NULL
  END,
  /* SI(TipoEvaluaci√≥n="EVALUACION EN CENTRALIZADO"; Estado_resultadoanalista; BUSCARV(...)) */
  CASE
    WHEN D.OPERACION IS NOT NULL THEN A.ESTADO_RESULTADOANALISTA
    ELSE
      CASE
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'DENEGADO'                         THEN 'DENEGADAS'
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'APROBADO'                         THEN 'ACEPTADAS'
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'FACA'                             THEN 'FACA'
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'DESESTIMADO'                      THEN 'DESACTIVADA'
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'ACEPTADA'                         THEN 'ACEPTADAS'
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'DENEGADO POR ANALISTA DE CREDITO' THEN 'DENEGADAS'
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'APROBADO POR ANALISTA DE CREDITO' THEN 'ACEPTADAS'
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'EN PROCESO'                       THEN 'EN PROCESO'
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'SOLICITUD CANCELADA'              THEN 'SOLICITUD CANCELADA'
        WHEN UPPER(TRIM(D.RESULTADOANALISTA)) = 'ENVIADO AL CENTRALIZADO'          THEN 'ENVIADO AL CENTRALIZADO'
        ELSE NULL
      END
  END
) AS ResultadoExcel
