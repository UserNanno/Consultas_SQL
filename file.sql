SELECT CODSOLICITUD, CODINTERNOCOMPUTACIONAL, TIPROLSOLICITUD, DESTIPROLSOLICITUD FROM CATALOG_LHCL_PROD_BCP.BCP_UDV_INT_VU.M_SOLICITUDINDIVIDUO
