CASE
    WHEN MTOAPROBADORENEGOCIADO IS NULL THEN 'SIN INF.'
    WHEN MTOAPROBADORENEGOCIADO < 1000 THEN '0 - 1,000'
    WHEN MTOAPROBADORENEGOCIADO < 2000 THEN '1,000 - 2,000'
    WHEN MTOAPROBADORENEGOCIADO < 3000 THEN '2,000 - 3,000'
    WHEN MTOAPROBADORENEGOCIADO < 5000 THEN '3,000 - 5,000'
    ELSE '>= 5,000'
END AS BRACKET_RENEGOCIADO



PctRenegociado =
DIVIDE(
    SUM('WEEKLY'[FLGAPROBADORENEGOCIADO]),
    CALCULATE(SUM('WEEKLY'[FLGAPROBADORENEGOCIADO]), ALL('WEEKLY'))
)
